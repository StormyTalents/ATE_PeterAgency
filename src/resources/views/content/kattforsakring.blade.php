<h2 class="text-danger">Varför kattförsäkring?</h2>
<p>
Du kan alltid ta din katt till en veterinär om den skadar sig eller blir sjuk. Men det kan bli otroligt dyrt om du inte har en kattförsäkring. Det gör att många som inte har försäkring istället väljer att avliva sin katt vid värre sjukdomar/olyckor. 
</p>
<p>
Genom att ha kattförsäkring kan din katt få den vård som behövs för att den ska kunna må bra och leva längre. När försäkringen utnyttjas betalar du enbart en självrisk.  
</p>

<h2 class="text-danger">Hur viktig är kattförsäkring?</h2>
<p>Försäkringsbolaget Agria anger att <strong>ungefär hälften av alla katter i Sverige är försäkrade</strong>. Det som främst avgör om kattförsäkringen är viktig att teckna är hur högt du värderar att kunna ge din katt den vård som behövs vid sjukdom/olycka. </p>
<ul>
<li>Hur mycket kostade katten att köpa? Är det värt att teckna en försäkring i förhållande till inköpspriset?</li>
<li>Hur gärna vill du kunna ge katten den vård som behövs för ett bättre liv efter olycka/sjukdom?</li>
</ul>
<h2 class="text-danger">Kattförsäkringens olika delar</h2>
<p>En kattförsäkring består i grunden av en veterinärvårdsförsäkring. Det är ”basen” som sedan kan byggas på med livförsäkring och olika tilläggsförsäkringar. </p>
<ul>
<li>Veterinärvårdsförsäkring - Om katten skadar sig eller blir sjuk, och ett besök sker hos legitimerad veterinär, är det veterinärvårdsförsäkringen som ger ersättning. Den täcker därmed undersökning och behandling upp till en viss summa per år. </li>
<li>Livförsäkring - Om katten avlider, eller bör avlivas enligt veterinär, ger livförsäkringen ersättning. Detta upp till att katten når en viss ålder. </li>
<li>Tilläggsförsäkringar - Tilläggsförsäkringar är försäkringar som kan ge ersättning vid tillfällen som försäkringsdelarna ovan inte täcker. Dessa kan även ge förhöjda ersättningsbelopp. Exempel på tilläggsförsäkringar är Reseförsäkring, Ersättning för medicin samt Ersättning vid rehabilitering. </li>
</ul>

<h2 class="text-danger">Vad kostar kattförsäkring?</h2>
<p>Det som främst avgör vad en kattförsäkring kostar är ras, kön, inköpspris, ålder, självrisknivå och ersättningsnivåer. Det är därmed svårt att säga vad en kattförsäkring kostar generellt. </p>
<div class="highlight border-success">
<p>Exempel: En kattförsäkring för en Helig Birma på 5 år tecknas hos Moderna Försäkringar. Priset blir i vårt test 58 kr per månad för deras lägsta nivå och 174 kr per månad för deras högsta nivå. Detta är då inte med livförsäkring.</p> </div>

<h3>Jämföra pris på kattförsäkring</h3>
<p>De flesta försäkringsbolag har uträknare på sin hemsida. Fyll i uppgifter om katten samt om dig som ägare och få ett prisförslag på två minuter. </p>

<div class="highlight border-info">

<p><i>”Söker du bara en grundläggande kattförsäkring utan speciella försäkringstillägg? Jämför priset hos flera bolag och välj det billigaste alternativet. Några dåliga kattförsäkringar finns inte i Sverige – bara olika bra” </i></p>
</div>

<h2 class="text-danger">Välja försäkring – Vad är viktigast för dig?</h2>
<p>När du jämför kattförsäkringar är det viktigt att du funderar över vad som är viktigast för dig. Är det enbart priset eller vill du ha en kattförsäkring med extra låg självrisk? </p>


<p><strong>Vanliga punkter som skiljer försäkringsbolagen åt:</strong></p>
<h3>Hur gammal får katten vara?</h3>
<p>En kattförsäkring kan tecknas från att katten är 6 veckor. Men det som skiljer är hur gammal katten maximalt kan vara när försäkringen tecknas samt om försäkringen upphör (eller ersättning minskas) vid viss ålder. </p>
<h3>Hur stor är ersättningen?</h3>
<p>Veterinärvårdsförsäkringen kan ge ersättning upp till ett specifikt belopp per år. Detta skiljer kraftigt med allt från 20 000 kr till 120 000 kr. En del bolag sänker ersättningsbeloppet när katten når en viss ålder – andra har samma ersättningsnivå oavsett ålder. </p>
<h3>Vad ingår – och vad kan tecknas som tillägg?</h3>
<p>Alla kattförsäkringar består av en veterinärvårdsförsäkring. Men det varierar i vilka tillfällen som ersättning kan ges. Det gäller framförallt kring Medicin, Rehabilitering, Röntgen och mer avancerade ingrepp. För allmän vård täcker alla veterinärvårdsförsäkringar. </p>
<p>Kontrollera även om livförsäkring ingår eller inte i grundpriset. Önskas något specifikt försäkringstillägg (exempelvis reseförsäkring eller användbarhetsförsäkring) kan detta avgöra vilket bolag som försäkring kan tecknas hos. </p>
<h3>Självrisk</h3>
<p>Självrisken är generellt uppbyggd av en fast avgift samt en procentuell del på kostnaden. Många gånger kan du välja på flera olika alternativ – vilket då även påverkar premien. Exempelvis kan fasta avgiften vara 750 - 3000 kr och rörliga delen 15 - 25 %. </p>
<h3>Pris</h3>
<p>För många är priset mest relevant. Delvis gäller ”du får vad du betalar för” men självklart är det lönsamt att jämföra priset hos flera bolag. Detta inte minst om du enbart vill ha en grundläggande försäkring utan specifika delar. </p>


