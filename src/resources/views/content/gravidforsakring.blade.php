<h3>Jämföra pris på barnförsäkring</h3>
<p>Som visas ovan är skillnaden inte stor mellan bolagen. Önskas ändå prisjämförelses är det lätt att få fram priserna direkt från bolagens hemsidor. Försäkringen har ett fast pris och är alltså inte beroende på var du bor, din ålder eller andra faktorer. </p>
<div class="highlight border-info">
	<p>Tips: Många försäkringsbolag ger kraftig rabatt på barnförsäkringen om man haft gravidförsäkring hos dem tidigare. Vet du redan nu var du kommer teckna barnförsäkringen kan det finnas en fördel att därmed teckna gravidförsäkringen på samma bolag. </p>
</div>

<h2 class="text-danger">OBS! – Glöm inte barnförsäkringen</h2>
<p>Även om en gravidförsäkring ger ett visst försäkringsskydd även efter förlossningen är det viktigt att en barnförsäkring tecknas så direkt som möjligt. Dessa ger ett betydligt bredare skydd. </p>
<h2 class="text-danger">Vilken försäkring ska jag välja?</h2>
<p>Det är ganska stor skillnad på de gravidförsäkringar som erbjuds. Detta både utifrån i vilka tillfällen som ersättning kan ges, ersättningsbelopp och vilka som omfattas av försäkringen. </p>
<h3>När börjar försäkringen gälla?</h3>
<p>Försäkringen för barnet kan börja gälla vecka 22 eller 23 men det finns även försäkringar som inte börjar gälla förrän vid förlossning. </p>
<p>Försäkringar för den gravida kan börja gälla allt från vecka 10 i graviditeten till att gälla 12 månader från att den tecknas oavsett när detta sker. </p>
<h3>När ges ersättning?</h3>
<p>Du kan få ersättning vid en rad tillfällen. Se ovan under rubriken ”Gravidförsäkringens olika delar”. Men skillnaden är stor hos bolagen. En del ger ersättning vid sjukdom, medicinsk invaliditet, diagnoser, olycksfall och sjukhusvistelse. Andra enbart vid vissa av dessa tillfällen. </p>
<h3>Ingår ärrersättning?</h3>
<p>En del bolag ger alltid ärrersättning (för barnet), andra enbart vid olycksfall medan ytterligare aldrig gör det. </p>
<h3>Hur stor är ersättningen?</h3>
<p>Ersättningsbeloppet varierar något mellan bolagen. Dessutom kan maximalt ersättningsbelopp variera inom olycksfallsförsäkringen beroende på invaliditetsgrad. </p>
<h3>Vilka omfattas?</h3>
<p>Inom olycksfallsförsäkringen kan även den andra föräldern täckas av försäkringsskyddet. Men det är inte alltid det är så. </p>
<h3>Vad kostar den?</h3>
<p>De flesta försäkringsbolag har en gratis gravidförsäkring. Men önskas ett större försäkringsskydd tecknas istället de som kostar. Man kan då räkna med ett pris på ca 1 200–1 600 kr. Med tanke på vilket försäkringsskydd som ges (med höga ersättningsnivåer vid sjukdom/olycka) kan det ses som ett prisvärt skydd fram till att barnförsäkring kan tecknas.  </p>

