			<h2 class="text-danger">Varför gravidförsäkring?</h2>
<p>Med gravidförsäkring blir både graviditeten, förlossningen och barnets första månader tryggare. Det är det enda sättet att försäkra ett ofött barn. </p>
<p>Försäkringen finns i två nivåer. Ett gratisalternativ som innehåller olycksfallsförsäkring och livförsäkring samt ett betalalternativ som har ett betydligt bredare skydd. Priset på dessa brukar vara på ca 1500 kr. </p>
<h2 class="text-danger">Hur viktig är gravidförsäkring?</h2>
<p>Eftersom det finns gratis gravidförsäkring finns ingen anledning att inte teckna denna vid graviditet. Väljs istället en gravidförsäkring med premie ges ett större ekonomiskt skydd. </p>
<ul>
<li>Välj minst en gratis gravidförsäkring</li>
<li>Välj betalalternativet för större försäkringsskydd</li>
</ul>
<p>Priset är ca 1 500 kr och för detta ges en grundligt ekonomiskt skydd som kan ge ersättning vid olycksfall, komplikationer eller om barnet har specifika diagnoser. Tillfällen då en ersättning kan vara behjälplig för att klara av vardagen bättre. </p>
<h2 class="text-danger">Gravidförsäkringens olika delar</h2>
<p>Gravidförsäkring är en av de få försäkringar som kan tecknas helt gratis. De flesta försäkringsbolag har två olika försäkringspaket där det minsta är gratis. </p>
<h3>Gratis gravidförsäkring – Vad ingår?</h3>
<ul>
<li>Olycksfallsförsäkring - Olycksfallsförsäkring för den gravida och barnet (ibland även för den andra föräldern). Däremot är försäkringsbeloppet relativt lågt.</li>
<li>Livförsäkring - Livförsäkring för föräldrarna och barnet. </li>
<li>Kristerapi - I vissa gratisalternativ ingår även Kristerapi. Det betyder samtalsterapi hos psykolog om en krisreaktion uppstått på grund av exempelvis kraftiga komplikationer, dödsfall eller att barnet föds med allvarliga skador. </li>
	</ul>

<h2>Gravidförsäkring med premie – Vad ingår?</h2>
<p>Denna försäkring har både olycksfall och livförsäkring (som ovan) men ofta med högre ersättningsnivåer. Dessutom ingår följande:</p>
<ul>
<li>Sjukförsäkring - Om barnet blir sjuk inom 6 månader efter förlossningen ges ersättning.</li>
<li>Diagnosförsäkring - En engångssumma betalas ut om specifika diagnoser kan sättas inom några månader efter förlossning. Det kan vara vissa hjärtfel, Downs syndrom, celebral pares osv. </li>
<li>Komplikationsförsäkring - Ger en lägre ersättning (upp till ett par tusen) vid komplikationer under graviditeten eller förlossningen. </li>
<li>Sjukhusvistelse - Kan ge ersättning per dag om den gravida mamman eller barnet behöver ligga på sjukhus i samband med graviditet/förlossning. </li>
</ul>
<h2 class="text-danger">Vad kostar en gravidförsäkring?</h2>
<p>Prisskillnaden mellan gravidförsäkringarna är relativt små. Det handlar enbart om någon hundralapp. Det är därmed viktigare att välja en försäkring man känner sig trygg med än att spara några hundra på att ta den billigaste. </p>

