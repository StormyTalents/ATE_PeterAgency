<h2>Hur viktig är inkomstförsäkringen?</h2>
<p>Det som primärt avgör om inkomstförsäkring behövs eller inte är din nuvarande månadslön. </p>
<p><strong>Har lite över 25 000 kr per månad bör inkomstförsäkring övervägas. </strong></p>
<p>Om du är med i A-kassan och blir arbetslös kommer du få 80 % av din lön i ersättning. Men detta enbart upp till en viss nivå. Så länge du har en bruttolön på under 25 025 kr kommer din ersättning att bli 80 % av lönen. Därmed behöver du inte en inkomstförsäkring utifrån dagens lönesituation. </p>
<p>Har du högre lön än 25 025 kr kommer A-kassans ersättning inte att täcka 80 % av lönen. Det är i detta läge en inkomstförsäkring ger ersättning för resterande del. Detta så att din totala ersättning vid arbetslöshet uppgår till 80 % av din ursprungliga lön. </p>
<p>Det finns även vissa fördelar med att ha inkomstförsäkring trots en lägre lönenivå. Efter viss tid (exempelvis 100 dagar) sänks ersättningsnivån från A-kassan. I detta fall kan inkomstförsäkringen täcka upp. Dessutom finns fördelen att man haft försäkringen länge när väl lönen höjs. Detta eftersom försäkringen har en kvalificeringstid (Tid du måste haft försäkringen innan den kan ge ersättning). Se gärna räkneexemplet nedan. </p>
<p>(Siffror gällande för 2019. Med reservation för ändringar)</p>
<h2>Räkneexempel</h2>
<div class="highlight border-success">
<p>Du har en bruttolön på 32 000 kr och har en inkomstförsäkring.</p>
<p>Första 100 dagarna:</p>
<ul>
<li>Ersättning från A-kassan – 14 501 kr</li>
<li>Ersättning från inkomstförsäkringen - 3797 kr</li>
<li>Total summa att få per månad – 18 298 kr</li>
</ul>
<p>101 - 200 dagarna:</p>
<ul>
<li>Ersättning från A-kassan – 12 323 kr</li>
<li>Ersättning från inkomstförsäkringen – 5975 kr</li>
<li>Total summa att få per månad – 18 298 kr</li>
</ul>
<p>201 – 300 dagarna:</p>
<ul>
<li> Ersättning från A-kassan – 12 323 kr</li>
<li>Ersättning från inkomstförsäkringen – 3830 kr</li>
<li>Total summa att få per månad – 16 153 kr</li>
</ul>
<p>Exemplet bygger på en inkomstförsäkring med 300 dagar ersättningstid med sänkt ersättning efter 200 dagar. </p>
<p>Hur många dagar som ersättning ges samt när eventuell sänkning av ersättningsbeloppet sker kan variera. </p>
</div>
<h2>Vad kostar en inkomstförsäkring?</h2>
<ul>
<li><h3>Har du redan via facket?</h3>
	<p>Hos många fackförbund ingår en inkomstförsäkring i medlemsavgiften. Den räcker gott och väl för de allra flesta medlemmar både gällande högsta ersättningsnivå och antal dagar ersättning kan ges. Önskas förbättrade villkor går det ofta att betala extra för tillägg. </p></li>
	<li><h3>Har du inte via facket?</h3>
		<p>Har du inte inkomstförsäkring via facket kan inkomstförsäkring tecknas hos ett flertal försäkringsbolag. Det som främst avgör priset är vilken inkomst du vill försäkra. Men det kan även avgöras av vilken A-kassa du är ansluten till. </p></li>
	</ul>

	<p><strong>Exempel från Solidab 2019:</strong></p>
<div class="highlight border-success">
<p>Lärare som är medlem i Lärarnas A-kassa med 32 000 kr i lön – 3720 kr per år></p>
<p>Elektriker som är medlem Elektrikernas A-kassa med 34 000 kr i lön – 4944 kr per år</p>
<p>Hotellpersonal som är medlem i Hotell- och restauranganställdas A-kassa med lön på 30 000 kr – 7140 kr</p>
<p>Kom ihåg att priset ovan enbart är ett exempel och att det kan variera beroende på bland annat antal ersättningsdagar. Det är även skillnad mellan olika försäkringsbolag. </p></div>

<h2>Välja inkomstförsäkring – Vad är viktigast?</h2>
<ul>
<li><h3>Ersättningstid</h3>
	<p>Med ersättningstid menas antalet dagar som går att få ersättning från inkomstförsäkringen. Det är en av de punkter som kan förbättras med tillägg till de försäkringar som ingår fackmedlemskap. Generellt är det även flera dagar på privata försäkringar. Detta är en av de viktigaste punkterna att jämföra. </p></li>
<li><h3>Maxlön att försäkra</h3>
	<p>Har du inte högre lön än 50 000 kr är detta inte något speciellt att kontrollera. Alla inkomstförsäkringar täcker nämligen upp till denna nivå och de allra flesta betydligt högre. </p></li>
<li><h3>Kvalifikationstid</h3>
	<p>Kvalifikationstid innebär hur länge du måste haft försäkringen innan du kan få ersättning. Det är vanligt att detta är 12 månader. Därmed kan det vara en fördel att teckna försäkring även om man har lön kring 25 000 kr. Detta utifrån att man då avverkat kvalifikationstiden till kommande år och löneförhöjning.</p></li>
</ul>

