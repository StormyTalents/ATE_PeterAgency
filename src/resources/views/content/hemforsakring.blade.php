<h2>Hur viktig är hemförsäkring?</h2>
<p>Hemförsäkringen kan anses vara den absolut viktigaste försäkringen. Den är inte obligatorisk – men borde nästan vara det. Enda gången som hemförsäkring inte bör tecknas är om du har försäkringsskydd av någon annans hemförsäkring (exempelvis att du fortfarande bor hemma hos föräldrarna). Hemförsäkring är viktig då den ger ett grundläggande skydd:</p>
<ul>
<li>På resan (Reseförsäkringen)</li>
<li>För egendom som blir stulet eller utsatt för skadegörelse</li>
<li>Vid rättstvister (Rättstvist)</li>
<li>Vid skadeståndskrav (Ansvarsförsäkringen)</li>
	</ul>
	<p>Allt för många tror att hemförsäkringen enbart ger ersättning om dyr egendom går sönder. Men skyddet är betydligt mycket bredare än så. </p>
	<h2>Hemförsäkringens olika delar</h2>
	<p>Hemförsäkringen har ett flertal olika försäkringsdelar vilka kan kategoriseras på följande sätt:</p>
	<h3>Säkerhet för dina saker</h3>
	<p>Egendomsskydd – Få ersättning om egendom blir stulen eller utsatt för skadegörelse. Detsamma gäller vid brand, vattenskada, rökskada osv. Ersättning ges även vid stöld utanför hemmet förutsatt att man inte varit oaktsam. </p>
	<h3>Säkerhet för dig på resan</h3>
	<p>Reseskydd – Alla hemförsäkringar har ett reseskydd som vanligtvis gäller under de första 45 dagarna utomlands. Försäkringen ger ekonomiskt skydd för kostnader vid sjukdom eller olyckshändelse. Det går även att få ersättning för bland annat försenat bagage eller om hemresan behöver bokas om för att hinna hem till allvarligt sjuk familjemedlem. </p>
	<h3>Säkerhet för dig i vardagen</h3>
	<p>Ansvarsskydd – Kan ersätta skadeståndskrav om detta ställs mot dig. Du kan exempelvis råkat skada en vattenledning hos grannen vilket skapat stora materiella skador. </p>
	<p>Drulle – Få ersättning för skador du själv är orsak till. Du kan exempelvis ha tappat en vas i golvet eller krossat en ruta när golvet skulle moppas. Detta försäkringsskydd ingår inte alltid i grundförsäkringen. </p>
	<p>Rättsskydd – Uppstår en tvist som dras till allmän domstol kan försäkringen delvis ersätta rättegångskostnaden och kostnaden för rättshjälp. </p>
	<p>Överfall – Ger ersättning om du blir överfallen och misshandlad. Gäller även om du utsatts för sexualbrott (Gäller däremot inte om gärningsmannen omfattas av samma försäkring)</p>
	<h3>Speciella regler kring stöldbegärlig egendom</h3>
	<p>Alla hemförsäkringar har speciella regler för stöldbegärlig egendom. Det gäller exempelvis smycken, datorer och mobiler. För att få ersättning för dessa vid stöld kan det krävas att vissa krav är uppfyllda. En del bolag ger ingen ersättning om stöldbegärlig egendom stjäls utanför bostaden. </p>
	<h3>Tilläggsförsäkringar</h3>
	<p>Det är mycket vanligt att tilläggsförsäkringar erbjuds till en hemförsäkring. Försäkringar som därmed skapar ett ekonomiskt skydd för en specifik händelse. Exempel är:</p>
	<p>Sakförsäkring – Vid enskilda föremål med högt värde som därmed inte omfattas av hemförsäkringens totala ersättningsbelopp. </p>
	<p>Resetillägg – Alla hemförsäkringar har reseförsäkring. Men ibland kan förbättrad skydd tecknas via extra resetillägg. </p>
	<p> Bostadsrättstillägg – Bor du i bostadsrätt bör alltid ett bostadsrättstillägg tecknas som täcker eventuella skador på föreningens egendom. </p>
	<p>ID-skydd – En försäkring som kan tecknas separat eller som tillägg till hemförsäkringen. Ger hjälp och ersättning vid ID-stöld. </p>
	<h2>Vad kostar en hemförsäkring?</h2>
	<p>Det som avgör priset på en hemförsäkring är bland annat adress, storlek på bostaden, boendeform, självrisknivå, värde på lösöre, eventuella tilläggsförsäkringar och säkerhet (larm, säkerhetsdörr, jordfelsbrytare). Det går därmed inte generellt att säga vad det kostar – enda vägen är att be om kostnadsfria offerter från bolagen. </p>
	<div class="highlight border-success">
<p>Exempel:</p>
<p>En bostadsrätt i ett bostadsområde i utkanten av medelstor stad ska försäkras. Bostadsrätten är på 150 kvadratmeter, har larm och en självrisk väljs på 1200 kr. Försäkringsbelopp väljs på 200 000 kr. </p>
<p>I en jämförelse av fem försäkringsbolag gavs lägsta årspremie på 2900 kr och högst kostnad på 4600 kr. </p>
</div>
<h2>Jämföra pris på hemförsäkring</h2>
<p>Oavsett vilken hemförsäkring du väljer så kommer du få ett bra försäkringsskydd. Däremot kan det självklart skilja i både självrisk, ersättningsnivåer och när ersättning kan betalas ut. Söker du enbart efter ett grundläggande skydd bör en prisjämförelse absolut genomföras. </p>
<p>Generellt är det inte mer än personnummer och antal personer som bor på adressen som behöver anges på försäkringsbolagens hemsidor. Via register kan de därmed se var du är skriven på för adress och därmed presentera ett prisförslag. </p>
<h2>Välja försäkring – Vad är viktigast för dig?</h2>
<p>Sett till vad hemförsäkringarna innehåller för försäkringsdelar så är de extremt lika. Du kan därmed vara säker på att få ett gott försäkringsskydd oavsett vilken försäkring som väljs. Men självklart finns skillnader. Detta bland annat på pris, ersättningsnivå samt vad som ingår i basutbudet och vad som därmed erbjuds i tilläggsförsäkringar. </p>
<h3>Vilka omfattas av försäkringen?</h3>
<p>Försäkringen täcker generellt försäkringstagaren och dess familj. Däremot kan det variera om försäkringsbolagen kräver att medförsäkrade måste vara skrivna på samma adress och/eller vara inskrivna på försäkringsbrevet. </p>
<h3>Självrisk</h3>
<p>Självrisken är ofta kring 1500 kr. </p>
<h3>Ersättningsnivå</h3>
<p>Välj ersättningsnivå när försäkringen tecknas. Hos vissa finns begränsningar (exempelvis max 1 miljon) medan det hos andra går att välja fullvärde. Med fullvärde finns ingen övre gräns för maxbelopp. </p>
<p>Kom ihåg att maximalt ersättningsbelopp skiljer sig från om stöld sker i bostaden eller utanför bostaden. </p>
<h3>Värdeminskning</h3>
<p>Vid ersättning räknar försäkringsbolaget ut ersättning utifrån marknadsvärde och eventuell värdeminskning. Främst är det på elektronik som värdeminskningen är stor. </p>
<h3>Pris</h3>
<p>Det är en tydlig skillnad i pris på hemförsäkringar (i likhet med alla försäkringar) hos de olika försäkringsbolagen. Delvis beror det på vilka som är deras primära kundgrupp, vid vilka tillfällen som ersättning ges och ersättningsnivå. Men även om man jämför ungefärligt lika försäkringar så skiljer sig priset. </p>
<h3>Vad ingår i hemförsäkringen – vilka tillägg behöver tecknas?</h3>
<p>En tydlig skillnad mellan försäkringsbolagen är hur de bygger upp sin hemförsäkring med olika försäkringsdelar. Vissa har färdiga ”paketlösningar” som därmed ersätter vid många olika tillfällen. Andra har smalare grundförsäkring och låter kunden bygga på med olika tilläggsförsäkringar. </p>
<p>Många personer vill exempelvis ha Allrisk (kallas ofta för Drulleförsäkring). Detta ingår i hemförsäkringen hos vissa bolag medan det hos andra behöver tecknas som tillägg. </p>
