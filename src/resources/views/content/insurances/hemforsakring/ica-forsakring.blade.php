<p>
    ICA erbjuder en komplett hemförsäkring där Allrisk ingår redan i grundpriset. En av styrkorna hos ICA är prisnivån. Hemförsäkringen har generellt låg premie, ger ICA-bonuspoäng och kan även aktivera ”Dubbelbonus”. Är du student rekommenderas deras hemförsäkring för studenter.
</p>
<h2>Innehåll och tillägg</h2>
<p>
    Hemförsäkringen hos ICA gäller enbart för dig som bor i hyresrätt eller bostadsrätt. Bor du i villa rekommenderas istället deras Husförsäkring (Villaförsäkring). 
</p>
<p>
    Försäkringsdelarna har stor likhet med andra bolags hemförsäkringar. Här ingår exempelvis Lösöre, Krisförsäkring, Reseförsäkring och Ersättning vid brand/inbrott/skadegörelse. 
</p>
<p>
    Fördelen med ICA är att Allrisk ingår i grundförsäkringen och att upp till 5000 kr i presentkort på ICA ges om kyl eller frys går sönder.
</p>
<h2>Försäkringstillägg</h2>
<p>
    Då Allrisk ingår i grundförsäkringen ges en komplett hemförsäkring som passar de allra flesta utan att några tillägg behöver tecknas. Men det går att förbättra hemförsäkringen på följande punkter:
</p>
<ul>
    <li>
    <h3>Hem Plus</h3>
    <p>
        Ökar maximal ersättning (upp till 100 000 kr) vid stöld eller skadegörelse på egendom som inte finns i bostaden. Sänker även självrisken och minskar åldersavdraget vid skador på stationär elektronik. Självrisken minskas också vid brand eller inbrott. Detta ner till 200 kr. 
    </p>
    </li><li>
    <h3>Rese Plus</h3>
    <p>
        Förbättrar reseförsäkringen med avbeställningsskydd, ersättning vid vistelse på sjukhus samt hemresa som på grund av sjukdom blir förskjuten. Självrisken inom reseskyddet försvinner. 
    </p>
    </li><li>
    <h3>Golftillägg</h3>
    <p>
        Kan ersätta stulen eller förstörd golfutrustning upp till 30 000 kr. Även ersättning vid stöld på golfbana eller hole in one. 
    </p>
    </li><li>
    <h3>Småbåtstillägg</h3>
    <p>
        Gäller för båt upp till 6 meter och max 15 hk.
    </p>
    </li>
</ul>
<h2>Prisförslag på hemförsäkring</h2>
<p>
    I likhet med andra försäkringsbolag är det mycket enkelt att få ett prisförslag på deras hemförsäkring. 
</p>
<ul>
    <li>Gå till ICA:s hemsida</li>
    <li>Ange personnummer, postnummer, bostadsyta och antal personer i hushållet</li>
    <li>Välj självrisk (1 500 - 6 000 kr) och ev. bostadsrättstillägg</li>
    <li>Se månadskostnad</li>
</ul>
<p>
Det är även möjligt att lägga till vissa tillägg (se ovan) vilket därmed höjer premien.
</p>
<h2>Varför välja ICA hemförsäkring?</h2>
<ul>
    <li>
        <h3>Extra billigt för studenter</h3>
        <p>
            ICA har tagit fram en hemförsäkring speciellt anpassad för studenter. Genom att exempelvis försäkringsnivån för lösöre maximalt är 200 000 kr för ensamhushåll och 300 000 kr för samboende kan premien pressas. En klar fördel för studenter som bor litet och söker en hemförsäkring till lågt pris.
        </p>
    </li>
    <li>
        <h3>Få dubbelbonus</h3>
        <p>
            Betalar du maten på ICA med något av deras kort, och även har försäkring hos ICA, ges automatiskt dubbel bonus på inköpen. Istället för 1% i bonus ges alltså 2% på alla inköp. Dessutom ges bonus på försäkringspremien.
        </p>
    </li>
    <li>
        <h3>Presentkort på mat om kyl/frys går sönder</h3>
        <p>
            Skulle kylen eller frysen gå sönder och maten behöva slängas bort ges ett presentkort på mat hos ICA på upp till 5000 kr.
        </p>
    </li>
</ul>
