<p>
    Med Moderna Försäkringar väljer du enkelt mellan tre olika nivåer på hemförsäkringen. Dessutom är hemförsäkringen anpassad utifrån om du bor i hyresrätt, bostadsrätt eller villa. 
</p>
<p>
    Välj Basnivån om du vill ha så billig försäkring som möjligt. Med Medium eller Large ingår exempelvis Allrisk, Utökat reseskydd samt flera Självriskreduceringar.
</p>
<h2>Innehåll och tillägg</h2>
<p>
    Hos Moderna Försäkringar tecknas hemförsäkring utifrån boendeform. Du väljer därmed mellan Hyresrättsförsäkring, Bostadsrättsförsäkring eller Villaförsäkring. Det är då paketlösningar som ger ett komplett boendeskydd. 
</p>
<ul>
    <li>
        <strong>Hyresrättsförsäkring</strong> – En klassisk hemförsäkring
    </li><li>
        <strong>Bostadsrättsförsäkring</strong> – Hemförsäkring med bostadsrättstillägg
    </li><li>
        <strong>Villaförsäkring</strong> – Hemförsäkring och Villaförsäkring i ett
    </li>
</ul>
<h2>Tre nivåer</h2>
<p>
    Hemförsäkringen kan tecknas i tre olika nivåer beroende på hur stort skydd som önskas. Här beskrivs hyres- och bostadsrättsförsäkringen. För information om nivåer på deras Villaförsäkring rekommenderas vår text om specifikt denna försäkring. 
</p>
<ul>
	<li>
        <h3>Bas</h3>
        <p>
            En grundläggande hemförsäkring för dig som enbart söker en grundtrygghet. Bland annat ingår Lösöre, Brand, Reseförsäkring, Krishantering, Överfallsskydd och Krishantering. 
            En fördel är att både ID-skydd, Underförsäkringsgaranti och Mobbning ingår. Delar som inte är alltid ingår i hemförsäkringar. 
        </p>
    </li>
    <li>
        <h3>Medium</h3>
        <p>
            Det som tillkommer på denna nivå är Allrisk samt Utökat reseskydd. Överlag rekommenderas Allrisk då man kan få ersättning om man själv råkar ha sönder något i hemmet. Det är lätt hänt att kaffet tappas över datorn eller att TV åker i golvet. 
        </p>
    </li>
    <li>
        <h3>Large</h3>
        <p>
            Det som tillkommer på denna nivå är Utökat bortaskydd (högre ersättning vid stöld från annan plats än bostaden, Självriskreducering (lägre självrisk vid brand och inbrott) samt Elektronikskydd (självriskreducering och minskat åldersavdrag) 
        </p>
    </li>
</ul>

<h2>Prisförslag på hemförsäkring</h2>
<p>
    Via hemsidan ges snabbt ett prisförslag oavsett vilken boendeform ni har. 
</p>
<p>
    <strong>Hyresrätt/Bostadsrätt</strong> – Besök hemsidan för hyresrätt/bostadsrättsförsäkring. Ange boendeform, postnummer och personnummer. Därefter anger du information kring boendet och hur många som bor i bostaden. Prisförslag ges sedan för de tre försäkringsnivåerna Bas, Medium och Large.
</p>
<p>
    <strong>Villaförsäkring</strong> – Besök hemsidan för villaförsäkring. Ange personnummer och därefter gatuadress och postnummer. Andra faktorer som anges är bostadens yta, om det finns källare samt om bostaden har larm. Därefter visas prisförslag för Bas, Medium och Large. 
</p>

<h2>Varför välja Moderna Försäkringars hemförsäkring?</h2>
<ul>
    <li>
        <h3>Mobbningsförsäkring ingår alltid</h3>
        <p>
            Vid mobbning av ditt barn ges upp till 10 besök för samtalsterapi. Dessutom en fast summa om Barn- och elevombudsmannen kräver skolan på skadestånd.
        </p>
    </li>
    <li>
        <h3>”ID-Stöld” ger alltid ersättning</h3>
        <p>
            Oavsett nivå på försäkringen ingår ersättning vid ID-stöld med upp till 20 000 kr för ombudskostnader.
        </p>
    </li>
    <li>
        <h3>Högst betyg på Ansvar och Rättskydd</h3>
        <p>
            I en oberoende jämförelse av hemförsäkringar får Moderna 4,3/5 av Konsumenternas på punkten ”Ansvar och Rättsskydd”. Därmed kom de i topp, tillsammans med ett par andra bolag, utifrån att de ger mest hjälp och störst ekonomisk ersättning inom denna kategorin. 
        </p>
    </li>
</ul>