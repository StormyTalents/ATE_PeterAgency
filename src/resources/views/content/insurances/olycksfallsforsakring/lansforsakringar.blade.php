<p>Länsförsäkringar erbjuder en bred olycksfallsförsäkring där du kan välja ett försäkringsbelopp som är bland de högre på marknaden. Du kan också välja bland 3 stycken olika omfattningar för att få ett skydd som passar för just dig och dina behov.</p>

<h2>Fördelar</h2>
<ul>
<li>Gäller dygnet runt</li>
<li>Bland de högre försäkringsbeloppen på marknaden</li>
<li>Gäller utan självrisk</li>
</ul>

<h2>Omfattning</h2>

<p>Länsförsäkringars olycksfallsförsäkring finns i 3 olika omfattningar:</p>

<ul>
<li>Olycksfall</li>
<li>Olycksfalls- och sjukförsäkring</li>
<li>Olycksfall 60 PLUS</li>
</ul>

<p>Försäkringen gäller dygnet runt och i hela världen (utanför Sverige i maximalt 12 månader).</p>

<h2>Pris och försäkringsbelopp</h2>

<p>Priset baseras på ålder, omfattning samt önskat försäkringsbelopp. Du kan se ditt pris på hemsidan genom att fylla i ditt personnummer.</p>

<p>Du kan välja mellan fem olika försäkringsbelopp:</p>

<ul>
<li>465 000 kr</li>
<li>930 000 kr</li>
<li>1 395 000 kr</li>
<li>1 860 000 kr</li>
<li>2 325 000 kr</li>
</ul>

<h2>Ålder och villkor</h2>

<p>Försäkringen kan tecknas upp till 60 år och gäller till 65 år.</p>

<h2>Passar försäkringen mig?</h2>

<p><strong>Passar dig som:</strong></p>

<ul>
<li>Vill kunna välja ett av de högsta försäkringsbeloppen på marknaden</li>
</ul>

<p><strong>Passar inte dig som:</strong></p>

<ul>
<li>Är över 65 år</li>
<li>Vill teckna en försäkring utan hälsodeklaration</li>
</ul>
