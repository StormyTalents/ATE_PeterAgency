<p>Ifs olycksfallsförsäkring erbjuder ett brett skydd där stort fokus ligger på den ekonomiska ersättningen där man bland annat kan ges dubbel ersättning vid invaliditet.</p>

<h2>Fördelar</h2>
<ul>
<li>Dubbel ersättning vid en fastställd invaliditetsgrad på minst 30%</li>
<li>Ersättning på 100 kr per dag i upp till 90 dagar vid sjukhusvistelse</li>
<li>Du väljer själv vilket försäkringsbelopp du önskar</li>
<li>Försäkringsbeloppet indexeras upp, d.v.s. blir lite högre för varje år. Om du köper en försäkring med ett försäkringsbelopp på 1,000,000 kronor i år så ska försäkringen lämna samma ersättning om 10 år</li>
</ul>

<h2>Omfattning</h2>
<p>Med Ifs olycksfallsförsäkring ges du ett skydd som innefattar bland annat sjukhusvistelse, tandskada, rese- och behandlingskostnader, dödsfallsersättning och kristerapi.</p>
<p>Försäkringen finns i 3 olika omfattningar:</p>

<ul>
<li>Sjuk- och olycksfall</li>
<li>Vuxen 50+</li>
<li>Olycksfall</li>
</ul>

<p>Försäkringen kan gälla dygnet runt eller endast på fritiden beroende på hur hög olycksfallsrisk det är i ditt yrke. Sjuk- och olycksfallsförsäkring och Vuxen 50+ gäller dygnet runt.</p>

<h2>Pris och försäkringsbelopp</h2>

<p>Prissättningen är individuell och baseras på vilken yrkeskategori du tillhör samt vilket/vilka försäkringsbelopp du önskar. Du kan se ditt pris genom att fylla i ditt personnummer på Ifs hemsida.</p>
<p>Du väljer själv vilket försäkringsbelopp du vill ha på din olycksfallsförsäkring och du kan välja ett belopp för bestående skada (medicinsk invaliditet) och ett belopp för arbetsförmåga (ekonomisk invaliditet). Försäkringsbeloppen du kan välja mellan är:</p>

<p>Bestående skada:</p>
<ul>
<li>500 000 kr</li>
<li>1 000 000 kr</li>
<li>1 500 000 kr</li>
<li>2 000 000 kr</li>
</ul>

<p>Förlorad arbetsförmåga:</p>
<ul>
<li>500 000 kr</li>
<li>1 000 000 kr</li>
<li>1 500 000 kr</li>
</ul>

<h2>Ålder och villkor</h2>
<p>Olycksfallsförsäkringen kan köpas av person mellan 0 - 70 år. Sjuk- och olycksfallsförsäkringen kan köpas av person som är mellan 18 och 50 år och Vuxen 50+ kan köpas av person som är mellan 50 och 70 år. Olycksfallsförsäkring och Vuxen 50+ kan du även köpa om du är mellan 70 och 80 år (under förutsättning att du redan har eller köper andra If-försäkringar).</p>
<p>Försäkringen gäller i hela världen med vissa begränsningar. Vid vistelse utanför Norden ersätts inte kostnader för tandskada och rese- och behandlingskostnader eller kristerapi.</p>

<h2>Passar försäkringen mig?</h2>

<p><strong>Passar dig som:</strong></p>
<ul>
<li>Vill ha en bred försäkring med en bra ekonomisk ersättning</li>
<li>Vill ha dubbel ersättning tills att du fyller 70 år</li>
<li>Vill ha en försäkring där försäkringsbeloppet är lika mycket från år till år</li>
</ul>

<p><strong>Passar inte dig som:</strong></p>
<ul>
<li>Vill ha ett skydd där kostnader för tandskada och rese- och behandlingskostnader eller terapi utanför Norden ersätts</li>
<li>Vill att ekonomisk invaliditet ska täckas efter 55 års ålder</li>
</ul>