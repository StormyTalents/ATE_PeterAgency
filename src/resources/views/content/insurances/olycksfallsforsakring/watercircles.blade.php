<p>WaterCircles är ett försäkringsbolag som erbjuder miljösmarta försäkringsalternativ. Deras olycksfallsförsäkring är prisvärd och låter dig själv välja omfattning.</p>

<h2>Fördelar</h2>

<ul>
<li>Du behåller samma försäkringsbelopp livet ut</li>
<li>Du är försäkrad dygnet runt och i hela världen</li>
<li>Försäkringen gäller utan självrisk</li>
<li>Ett miljövänligare försäkringsalternativ</li>
</ul>

<h2>Omfattning</h2>

<p>Försäkringen ersätter bland annat sjukhusvistelse, vanprydande ärr, medicinsk invaliditet, krishjälp och dödsfall.</p>

<p>Försäkringen gäller dessutom i hela världen, vid vistelse inom EU/ESS i upp till ett år och vid vistelse utanför i upp till 90 dagar.</p>

<h2>Pris och försäkringsbelopp</h2>

<p>WaterCircles olycksfallsförsäkring finns i fyra olika prisnivåer där det som skiljer sig är försäkringsbeloppet.</p>

<p><strong>Olycksfall – S:</strong></p>

<ul>
<li>Försäkringsbelopp: 250 000 kr</li>
<li>Pris: 39 kr</li>
</ul>

<p><strong>Olycksfall – M:</strong></p>

<ul>
<li>Försäkringsbelopp: 500 000 kr</li>
<li>Pris: 60 kr</li>
</ul>

<p><strong>Olycksfall – L:</strong></p>

<ul>
<li>Försäkringsbelopp: 750 000 kr</li>
<li>Pris: 78 kr</li>
</ul>

<p><strong>Olycksfall – XL:</strong></p>

<ul>
<li>Försäkringsbelopp: 1 000 000 kr</li>
<li>Pris: 100 kr</li>
</ul>

<h2>Ålder och villkor</h2>

<p>Försäkringen kan tecknas mellan 0 - 74 år och gäller livet ut. För att teckna försäkringen måste den försäkrade ha en aviseringsadress i Sverige.</p>

<h2>Passar försäkringen mig?</h2>

<p><strong>Passar dig som:</strong></p>

<ul>
<li>Vill vara försäkrad dygnet runt och i hela världen</li>
<li>Vill behålla samma försäkringsbelopp livet ut</li>
<li>Vill ha en försäkring som gäller livet ut</li>
<li>Vill ha en prisvärd försäkring</li>
<li>Söker ett miljösmartare försäkringsalternativ</li>
</ul>


<p><strong>Passar inte dig som:</strong></p>

<ul>
<li>Vill ha en försäkring som täcker ekonomisk invaliditet</li>
<li>Söker ett högre försäkringsbelopp än 1 000 000 kr</li>
</ul>
