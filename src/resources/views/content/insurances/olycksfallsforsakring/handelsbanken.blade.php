<h2>Omfattning</h2>

<p>Försäkringen ger bland annat ersättning vid resekostnader, tandskada, medicinsk invaliditet, ekonomisk invaliditet och dödsfall.</p>

<p>Däremot så får man ingen ersättning vid sjukhusvistelse vilket är något majoriteten av alla andra olycksfallsförsäkringar ersätter. Dessutom så får man ingen ersättning vid ärr och beloppet som utbetalas vid dödsfall är bland de lägre på marknaden.</p>

<h2>Pris och försäkringsbelopp</h2>

<p>Handelsbanken anger försäkringsbelopp och pris både i kronor och prisbasbelopp.</p>

<p>Priset baseras bland annat på vilket försäkringsbelopp man väljer, ens ålder och hur prisbasbeloppet utvecklas.</p>

<p>Månadspremier beräknade utifrån 10 prisbasbelopp (motsvarar 465 000 kronor år 2019):</p>

<ul>
<li>20 år = 27 kr/mån</li>
<li>30 år = 31 kr/mån</li>
<li>40 år = 44 kr/mån</li>
<li>50 år = 86 kr/mån</li>
<li>60 år = 190 kr/mån</li>
<li>65 år = 268 kr/mån</li>
</ul>

<p><strong>Valbara försäkringsbelopp:</strong></p>

<ul>
<li>227 500 kr</li>
<li>455 000 kr</li>
<li>682 500 kr</li>
<li>910 000 kr</li>
</ul>

<h2>Ålder och villkor</h2>

<p>Handelsbankens olycksfallsförsäkring kan tecknas mellan 18 – 64 år och gäller till dess att försäkringstagaren fyller 65 år. För att kunna teckna försäkringen måste du vara bosatt i Sverige.</p>

<p>Du kan medförsäkra din partner och barn som är hemmavarande och är under 18 år.</p>

<h2>Passar försäkringen mig?</h2>

<p><strong>Passar dig som:</strong></p>

<ul>
<li>Söker ett billigare alternativ</li>
<li>Vill kunna medförsäkra din partner och dina barn</li>
</ul>

<p><strong>Passar inte dig som:</strong></p>

<ul>
<li>Vill ha ersättning vid sjukhusvistelse och vid ärr</li>
<li>Vill att försäkringsbeloppet stannar på samma nivå</li>
<li>Söker lite högre försäkringsbelopp</li>
</ul>
