<p>Handelsbanken erbjuder en olycksfallsförsäkring som hör till de billigaste alternativen på marknaden. Du kan medförsäkra din partner och dina barn till en låg kostnad.</p>

<p>Tyvärr så saknar försäkringen vissa delar som gör att vårt betyg blir lite sämre. Bland annat så är det högsta valbara försäkringsbeloppet lägre än de flesta andra alternativen på marknaden. Dessutom så sänks försäkringsbeloppet redan vid 46 års ålder.</p>

