<p>Gjensidige är det tredje största försäkringsbolaget i Norden och har över 200 års erfarenhet. Hos Gjensidige hittar du en rad olika nischade försäkringar. Med deras olycksfallsförsäkring ges du ett brett skydd som gäller dygnet runt.</p>

<h2>Fördelar</h2>

<ul>
<li>Du kan själv välja försäkringsbelopp</li>
<li>Ersättning i upp till 365 dagar vid sjukhusvistelse</li>
<li>Ersätter skador som uppstår vid riskfyllda aktiviteter</li>
<li>Försäkringen gäller upp till 75 års ålder</li>
<li>Ett försäkringsbolag med över 200 års erfarenhet</li>
</ul>

<h2>Omfattning</h2>

<p>Gjensidiges olycksfallsförsäkring ger bland annat ersättning vid medicinsk och ekonomisk invaliditet, sjukhusvistelse, dödsfall, resekostnader, läke- och tandskadekostnader och rehabilitering.</p>

<p>Vid sjukhusvistelse kan du få ersättning i upp till 365 dagar. Hos många andra försäkringsbolag är 180 dagar en vanlig övre gräns.</p>

<p>En annan sak som sticker ut är det höga engångsbelopp man kan få vid vissa skador där man kan ges en ersättning på upp till 50 000 kronor. Här erbjuder Gjensidige en högre ersättning än många andra försäkringsbolag.</p>

<h2>Pris och försäkringsbelopp</h2>

<p>Prissättningen är individuell och baseras på ålder och önskat försäkringsbelopp. Du kan beräkna ditt pris på Gjensidiges hemsida genom att fylla i ditt personnummer samt önskat försäkringsbelopp.</p>

<p>Du kan välja mellan följande försäkringsbelopp:</p>

<ul>
<li>500,000 kronor</li>
<li>750,000 kronor</li>
<li>1,000,000 kronor</li>
<li>1,500,000 kronor</li>
</ul>

<h2>Ålder och villkor</h2>

<p>Försäkringen kan tecknas upp till 74 år och gäller till dess att du fyller 75 år.</p>

<p>Försäkringen gäller inom Norden och vid resa i hela världen under högst ett år. Värt att notera är att utanför Norden gäller försäkringen endast för vanprydande ärr, medicinsk och ekonomisk invaliditet och dödsfall.</p>

<h2>Passar försäkringen mig?</h2>

<p><strong>Passar dig som:</strong></p>
<ul>
<li>Söker en försäkring som ger ett brett skydd</li>
<li>Vill ha en försäkring som gäller till dess att du fyller 75 år</li>
<li>Själv vill kunna välja försäkringsbelopp</li>
<li>Vill ha en försäkring som ersätter eventuella skador som uppstår vid riskfyllda aktiviteter</li>
</ul>

<p><strong>Passar inte dig som:</strong></p>
<ul>
<li>Vill ha ett fullt skydd utomlands som inte begränsar vissa moment</li>
<li>Vill att försäkringsbeloppet för ekonomisk invaliditet stannar på samma nivå under hela försäkringstiden</li>
<li>Vill ha ett lite billigare alternativ</li>
</ul>