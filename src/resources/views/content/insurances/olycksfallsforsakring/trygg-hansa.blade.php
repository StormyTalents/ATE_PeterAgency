<p>Med Trygg-Hansas olycksfallsförsäkring kan du välja mellan 3 olika omfattningar: sjuk- och olycksfall, olycksfall och olycksfall för 55+. Det ger dig möjligheten att välja ett skydd som passar för just dig och dina behov.</p>

<h2>Fördelar</h2>

<ul>
<li>Välj vilken omfattning du önskar för att få en försäkring som passar dina behov</li>
<li>Du väljer själv vilket försäkringsbelopp du vill ha</li>
<li>Försäkringsbeloppet sänks inte vid en viss ålder</li>
</ul>

<h2>Omfattning</h2>

<p>Trygg-Hansas olycksfallsförsäkring ger bland annat ersättning vid medicinsk invaliditet, rehabilitering, sjukhusvistelse och för läke-, rese- och tandskadekostnader efter olycksfall.</p>

<p>Du kan välja att köpa till en eller två tilläggsförsäkringar som ett komplement till ursprungsförsäkringen:</p>

<ul>
<li>Vissa sjukdomar</li>
<li>Privatvård</li>
</ul>

<h2>Pris och försäkringsbelopp</h2>

<p>Trygg-Hansa erbjuder Individuell prissättning som baseras på ålder samt att du behöver svara på tre frågor gällande om du är svensk medborgare, är fullt arbetsför och anställd. Du kan räkna ut ditt pris på Trygg-Hansas hemsida.</p>

<p>De olika försäkringsomfattningarna erbjuder olika försäkringsbelopp.</p>

<p><strong>Sjuk och olycksfall:</strong></p>

<ul>
<li>400 000 kr</li>
<li>600 000 kr</li>
<li>800 000 kr</li>
<li>1 000 000 kr</li>
</ul>

<p><strong>Olycksfall:</strong></p>

<ul>
<li>600 000 kr</li>
<li>800 000 kr</li>
<li>1 000 000 kr</li>
</ul>

<p><strong>Olycksfall 55+:</strong></p>

<ul>
<li>200 000 kr</li>
<li>600 000 kr</li>
</ul>

<p>Försäkringsbeloppet kan ändras under försäkringstiden fram till din 55-årsdag.</p>

<h2>Ålder och villkor</h2>

<p>Sjuk- och olycksfallsförsäkringen och olycksfallsförsäkringen kan tecknas av dig som är mellan 18 - 54 år och gäller till dess du fyller 65 år. Olycksfall 55+ kan tecknas av dig som har fyllt 55 år och kan behållas livet ut.</p>

<p>Försäkringarna gäller dygnet runt och även utomlands, men utanför Norden får du inte ersättning för läke-, rese-, tandskade- och merkostnader.</p>

<h2>Passar försäkringen mig?</h2>

<p><strong>Passar dig som:</strong></p>

<ul>
<li>Vill kunna välja en omfattning som passar just dina behov</li>
<li>Själv vill kunna välja försäkringsbelopp</li>
<li>Söker en försäkring där försäkringsbeloppet inte sänks vid en viss ålder</li>
</ul>

<p><strong>Passar inte dig som:</strong></p>

<ul>
<li>Vill att kostnader utanför Norden täcks</li>
<li>Vill att ekonomisk invaliditet ersätts</li>
<li>Vill ha en försäkring som ersätter alla typer av riskfyllda aktiviteter</li>
</ul>