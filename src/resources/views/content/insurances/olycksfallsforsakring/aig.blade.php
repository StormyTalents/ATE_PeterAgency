<p>AIG erbjuder en rad olika försäkringar för att tillgodose olika behov. Deras olycksfallsförsäkring ger ett bra ekonomiskt skydd om du drabbas av en olycka.</p>

<h2>Fördelar</h2>
<ul>
<li>Premien är densamma oavsett ålder</li>
<li>Skattefri ersättning utan självrisk</li>
<li>Du får ersättning även om du har andra försäkringar</li>
<li>Hög ersättning vid dödsfall</li>
<li>Gäller till 75 års ålder</li>
</ul>

<h2>Omfattning</h2>

<p>När du tecknar olycksfallsförsäkring hos AIG kan du välja mellan två olika nivåer: silver och guld. Du kan då få ett skydd som passar just dina behov.</p>
<p>Försäkringen ger bland annat ersättning vid dödsfall, medicinsk invaliditet, sjukhusvistelse och benbrott.</p>
<p>Försäkringen gäller dygnet runt och gäller i hela världen, med undantag för länder som omfattas av internationella sanktioner.</p>

<h2>Pris och försäkringsbelopp</h2>

<p>Försäkringen kostar 63 kr/mån om du väljer silvernivån och 90 kr/mån om du väljer guldnivån. Om du medförsäkrar din partner blir totalkostnaden 120 kr/mån på silvernivån och 171 kr/mån på guldnivån.</p>

<p>Försäkringsbeloppet ligger på 500 000 kronor.</p>

<h2>Ålder och villkor</h2>

<p>För att kunna teckna försäkringen så krävs det att du och din eventuella partner är folkbokförd/a i Sverige. För att medförsäkra din partner (make, maka, sambo) krävs att ni är folkbokförda på samma adress. Försäkringen kan tecknas mellan 18-70 år och gäller fram till dess att försäkringstagaren fyller 75 år.</p>

<h2>Passar försäkringen mig?</h2>

<p><strong>Passar dig som:</strong></p>

<ul>
<li>Vill ha en försäkring som gäller dygnet runt och i hela världen (med undantag för länder som omfattas av sanktioner)</li>
<li>Vill ha en försäkring som gäller till 75 år</li>
</ul>

<p><strong>Passar inte dig som:</strong></p>

<ul>
<li>Vill att tandskador, förslitning eller överansträngning ska ersättas</li>
<li>Vill kunna välja mellan flera olika försäkringsbelopp</li>
</ul>