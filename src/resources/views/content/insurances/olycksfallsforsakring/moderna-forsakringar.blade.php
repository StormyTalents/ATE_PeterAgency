<p>Moderna Försäkringar erbjuder en prisvärd personförsäkring där du kan välja ett av tre paket beroende på hur bred omfattning du önskar.</p>

<h2>Fördelar</h2>
<ul>
<li>Försäkringen gäller helt utan självrisker</li>
<li>Högt ersättningsbelopp vid vissa skador</li>
<li>Försäkringen gäller livet ut</li>
</ul>

<h2>Omfattning</h2>

<p>Du kan välja mellan tre olika paket:</p>
<ul>
<li>Olycksfallsförsäkring - En försäkring som ersätter skador som uppkommer vid skador på grund av olycksfall och som kan ge ersättning vid medicinsk invaliditet.</li>
<li>Sjuk- och Olycksfall Medium - En försäkring som utöver ersättning vid olycksfall ger ersättning vid vissa sjukdomar</li>
<li>Sjuk- och Olycksfall Large - Modernas mest omfattande personförsäkring som ger dig ersättning vid medicinsk- och ekonomisk invaliditet.</li>
</ul>

<p>Om du är mellan 18 - 54 år och är bosatt i Sverige kan du dessutom teckna till en livförsäkring om du har tecknat en Medium eller Large.</p>

<p>Alla försäkringsomfattningarna gäller dygnet runt och gäller i Norden samt i högst ett år i resten av världen.</p>

<h2>Pris och försäkringsbelopp</h2>

<p>Priset är individuellt och baseras på försäkringsbelopp, typ av arbete och ålder.</p>

<p>De försäkringsbelopp som går att välja mellan är:</p>

<ul>
<li>600 000</li>
<li>900 000</li>
<li>1 200 000</li>
<li>1 500 000</li>
</ul>

<p>Om du samlar dina försäkringar hos Moderna Försäkringar kan du få upp till 20% rabatt på din premie.</p>

<h2>Ålder och villkor</h2>

<p>Försäkringen kan tecknas mellan 18 - 55 år och gäller livet ut. För att kunna teckna försäkringen måste du vara folkbokförd och bosatt i Sverige.</p>

<h2>Passar försäkringen mig?</h2>

<p><strong>Passar dig som:</strong></p>
<ul>
<li>Söker en prisvärd olycksfallsförsäkring med ett brett skydd</li>
<li>Vill ha en olycksfallsförsäkring som gäller livet ut</li>
</ul>

<p><strong>Passar inte dig som:</strong></p>
<ul>
<li>Vill att ekonomisk invaliditet ska ersättas</li>
<li>Vill att försäkringsbeloppet stannar på samma nivå under försäkringstiden</li>
</ul>
