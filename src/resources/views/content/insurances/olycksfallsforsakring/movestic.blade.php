<p>Hos Movestic kan du välja mellan att teckna en olycksfallsförsäkring separat eller som en del av ett paket. Varje paket är anpassat efter olika behov. I övrigt kan vi inte hitta mycket som sticker ut jämfört med andra olycksfallsförsäkringar på marknaden.</p>

<h2>Fördelar</h2>

<ul>
<li>Kan tecknas från 16 år</li>
<li>Du kan välja ett paket av försäkringar som passar just dina behov</li>
</ul>

<h2>Omfattning</h2>

<p>Movestics olycksfallsförsäkring ger bland annat ersättning vid läkekostnader, tandskadekostnader, resekostnader, medicinsk invaliditet, ekonomisk invaliditet och dödsfall.</p>

<p>Däremot så betalar Movestic inte ut någon ersättning vid sjukhusvistelse vilket är något de flesta andra försäkringsbolagen gör.</p>

<p>Du kan välja mellan att teckna en olycksfallsförsäkring separat eller något av följande färdiga paket:</p>

<ul>
<li>Singel: olycksfallsförsäkring och allvarlig sjukdomsförsäkring</li>
<li>Partner: olycksfallsförsäkring, livförsäkring och allvarlig sjukdomsförsäkring</li>
<li>Familj: olycksfallsförsäkring, livförsäkring, barnolycksfallsförsäkring och allvarlig sjukdomsförsäkring</li>
</ul>

<p>Olycksfallsförsäkringen gäller dygnet runt och i hela världen (under maximalt 1 år vid vistelse utanför Norden).</p>

<h2>Pris och försäkringsbelopp</h2>

<p>Priset på olycksfallsförsäkringen sätts individuellt och baseras på ålder och önskat försäkringsbelopp. När du tecknar försäkringen måste du besvara en hälsodeklaration. På Movestics hemsida kan du räkna ut ditt pris genom att fylla i ditt personnummer.</p>

<p>När du tecknar försäkringen kan du välja mellan 3 olika försäkringsbelopp:</p>

<ul>
<li>1 000 000 kr</li>
<li>1 500 000 kr</li>
<li>2 000 000 kr</li>
</ul>

<p>Viktigt att notera är att försäkringsbeloppen sänks efter att den försäkrade har fyllt 46 år. Medicinsk invaliditet sänks med 2,5% och per år och ekonomisk invaliditet sänks med 5% per år. I det här avseendet hör Movestics olycksfallsförsäkring till de sämre alternativen på marknaden.</p>

<h2>Ålder och villkor</h2>

<p>För att kunna teckna försäkringen måste du vara mellan 16 – 66 år och den gäller fram till dess att du som försäkrad fyller 67 år. Du som tecknar försäkringen måste dessutom vara folkbokförd i Sverige och vara inskriven i svensk försäkringskassa.</p>

<h2>Passar försäkringen mig?</h2>

<p><strong>Passar dig som:</strong></p>

<ul>
<li>Vill kunna välja ett paket med två eller fler försäkringar</li>
<li>Vill ha en försäkring som går att teckna från 16 år</li>
</ul>

<p><strong>Passar inte dig som:</strong></p>

<ul>
<li>Vill ha rätt till ersättning vid sjukhusvistelse</li>
<li>Vill att försäkringsbeloppet ligger kvar på samma nivå</li>
<li>Vill ha en försäkring som kan tecknas utan hälsodeklaration</li>
<li>Söker ett högre ersättningsbelopp vid dödsfall</li>
</ul>
