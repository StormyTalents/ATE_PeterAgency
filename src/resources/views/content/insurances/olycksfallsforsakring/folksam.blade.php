<p>Med en olycksfallsförsäkring från Folksam har du ett tryggt och bra skydd om oturen är framme. Folksam är ett kundägt företag vilket ger dig som kund flera fördelar, bland annat att du får återbäring på en del av den vinst företaget har gjort under året.</p>

<h2>Fördelar</h2>

<ul>
<li>Du kan själv välja en omfattning som passar dina behov</li>
<li>Bra ersättning vid tandskador</li>
<li>Fortsätter att gälla även efter att du har fyllt 65 år</li>
</ul>

<h2>Omfattning</h2>

<p>Folksams olycksfallsförsäkring ger ersättning vid bland annat sjukhusvistelse, rehabilitering, tandskador, invaliditet, dödsfall och krisbehandling.</p>
<p>Du kan själv välja mellan 3 olika försäkringsbelopp för att du ska få ett skydd som passar för just dig.</p>

<h2>Pris och försäkringsbelopp</h2>

<p>Prissättningen på försäkringen är individuell och beror på försäkringens omfattning, försäkringsbelopp och din ålder. På Folksams hemsida kan du se ditt pris genom att fylla i ditt fullständiga personnummer.</p>
<p>De försäkringsbelopp som går att välja mellan är:</p>

<ul>
<li>500 000 kronor</li>
<li>1 000 000 kronor</li>
<li>2 000 000 kronor</li>
</ul>

<h2>Ålder och villkor</h2>

<p>För att kunna omfattas av Folksams olycksfallsförsäkring måste du vara folkbokförd och befinna dig i Sverige. Försäkringen kan tecknas från och med att man har fyllt 16 år och ingen övre åldersgräns.</p>

<p>För att få teckna tilläggsförsäkring för</p>

<ul>
<li>ekonomisk invaliditet får du inte ha fyllt 56 år</li>
<li>diagnosförsäkring får du inte ha fyllt 76 år.</li>
</ul>

<h2>Passar försäkringen mig?</h2>
<p><strong>Passar dig som</strong></p>

<ul>
<li>Själv vill kunna välja ett försäkringsbelopp som passar just dina behov</li>
<li>Söker en försäkring som inte har någon övre åldersgräns</li>
<li>Vill vara en del av ett kundägt försäkringsbolag där du som kund får återbäring</li>
</ul>

<p><strong>Passar inte dig som</strong></p>

<ul>
<li>Söker en försäkring som ger ersättning vid exempelvis ryggskott, diskbråck och muskelbristning</li>
<li>Söker ett lite billigare alternativ av olycksfallsförsäkring</li>
</ul>