<p>”Vi vill bidra till en tryggare vardag för dig och din familj, var ni än är.”</p>
<p>ICA Banken har en mängd olika försäkringar utformade för att passa gemene man. Deras olycksfallsförsäkring är både omfattande och prisvärd. Du kan dessutom spara både tid och pengar genom att medförsäkra flera familjemedlemmar samtidigt.
</p>
<h2>Fördelar:</h2>
<ul>
<li>Gäller utan självrisk dygnet runt och du har samma skydd utomlands i upp till 12 månader</li>
<li>Omfattande &amp; prisvärd</li>
<li>Medförsäkra partner och barn till förmånligt pris.</li>
</ul>
<h2>Omfattning</h2>
<p>ICA Bankens olycksfallsförsäkring täcker det mesta en vanlig person vill att sin olycksfallsförsäkring ska innefatta. Den ger ersättning vid till exempel tandskador, sveda och värk, rehabilitering, behandlingar, sjukhusvistelse, invaliditet och dödsfall.</p>
<p>Några guldkorn är att du slipper självrisk samt att försäkringen gäller vid vistelse utomlands upp till 12 månader. Att en olycksfallsförsäkring gäller utomlands är långt ifrån en självklarhet och därför ett stort plus.</p>
<p>Annat som är värt att nämna är att du kan få en hög ersättning vid tandskada samt att om du vistas på sjukhus till följd av en olycka kan få en daglig summa i upp till 365 dygn. Vissa försäkringar ger endast ersättning för sjukhusvistelser upp till ex 180 dagar.</p>
<h2>Pris och försäkringsbelopp</h2>
<p>Du kan teckna försäkringen till ett förmånligt pris för hela familjen. Det gäller för upp till 12 familjemedlemmar där minst en person och högst två ska vara vuxna.</p>
<p><strong>Månadskostnad:</strong></p>
<ul>
<li>1 person: 85 kr/mån</li>
<li>2 personer: 169 kr/mån</li>
<li>3-4 personer: 199 kr/mån</li>
<li>5-12 personer: Tillägg på 50 kr/månad per ytterligare person.</li>
</ul>
<p>Försäkringsbeloppet och därmed den högsta ersättningen ligger på <strong>1 000 000 kr för vuxna och 500 000 kr</strong> för barn.</p>
<h2>Ålder och villkor</h2>
<p>Du behöver bo i Sverige och vara mellan 18 och 65 för att kunna omfattas av ICA olycksfallsförsäkring. Undantagsfall beträffande ålder är om du vill teckna en medförsäkring för ogifta barn (biologiskt, styvbarn eller adopterade) under 25 år som bor på samma adress som du eller med den andre föräldern.</p>
<h2>Förmåner med ICAs bankkort</h2>
<p>Har nu något av ICAs bankkort kan den här försäkringen även vara ett bra val för dig. Det är nämligen så att om du betalar med ett av ICAs bankkort får du dubbel bonus på hela försäkringskostnaden.</p>
<h2>Passar försäkringen mig?</h2>
<p><strong>Passar dig som: </strong></p>
<ul>
<li>Vill ha en prisvärd försäkring som omfattar de flesta vanliga olycksfallsskador som kan uppstå</li>
<li>Vill ha en smidig och tydlig försäkring</li>
<li>Gärna sparar pengar när du medförsäkrar barn eller partner.</li>
<li>Har något av ICAs bankkort</li>
<li>Tycker om att resa</li>
</ul>
<p><strong>Passar inte dig som:</strong></p>
<ul>
<li>Är över 65 år</li>
<li>Vill kunna välja mellan flera olika försäkringsbelopp</li>
</ul>