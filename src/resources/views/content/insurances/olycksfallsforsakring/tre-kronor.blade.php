<p>Försäkringsbolaget Tre Kronor samarbetar med Swedbank och flera fristående sparbanker. Utöver fördelen med att kunna teckna olycksfallsförsäkringen från 16 år så kan vi inte hitta mycket som sticker ut jämfört med de flesta andra olycksfallsförsäkringarna på marknaden.</p>

<h2>Fördelar</h2>

<ul>
<li>Kan tecknas från 16 år</li>
<li>15% rabatt vid tecknande av flera försäkringar</li>
</ul>

<h2>Omfattning</h2>

<p>Tre kronors olycksfallsförsäkring ger bland annat ersättning vid sjukhusvistelse, tandskada, ekonomisk invaliditet, medicinsk invaliditet, rehabilitering och dödsfall.</p>

<p>Försäkringen gäller dygnet runt och i hela världen. Utanför Norden gäller försäkringen i maximalt 12 månader.</p>

<h2>Pris och försäkringsbelopp</h2>

<p>För att teckna försäkring via Tre Kronor så vänder du dig till din bank. Du kan välja mellan att teckna försäkringen via telefon eller via ett bankkontor.</p>

<ul>
<li>500 000 kr – 73 kr/månad</li>
<li>1 000 000 kr - 95 kr /månad</li>
<li>2 000 000 kr - 142 kr/månad</li>
</ul>

<p>Om du tecknar både olycksfalls-, bil- och hemförsäkring hos Tre kronor får du 15% rabatt.</p>

<h2>Ålder och villkor</h2>

<p>Tre kronors olycksfallsförsäkring kan tecknas mellan 16 – 64 år.</p>

<p>Försäkringen gäller livet ut, men efter 65 år så förändras omfattningen en aning. Ingen hälsodeklaration krävs för att teckna försäkringen.</p>

<h2>Passar försäkringen mig?</h2>

<p><strong>Passar dig som:</strong>

<ul>
<li>Vill ha en försäkring som gäller från 16 år</li>
<li>Vill ha rabatt när du tecknar flera försäkringar</li>
</ul>

<p><strong>Passar inte dig som:</strong></p>

<ul>
<li>Behålla samma skydd efter 65 års ålder</li>
</ul>
