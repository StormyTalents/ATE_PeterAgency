<p>Trygg-Hansa erbjuder en bilförsäkring som är rankad högst hos Konsumenternas Försäkringsbyrå. De har en app där du kan fota skador, boka tider och följa reparationen av din bil.</p>

<p>Om du har hyrbilstillägget kan du dessutom få en hyrbil hemkörd.</p>

<h2>Varför välja Trygg-Hansa bilförsäkring?</h2>

<ul>
<li>Topprankad hos Konsumenternas Försäkringsbyrå</li>
<li>Du slipper turer till en verkstad</li>
<li>Märkesförsäkringar speciellt framtagna för vissa bilmärken</li>
</ul>

<h2>Innehåll</h2>

<p>Du kan välja mellan 4 olika försäkringar: Trafik, Halv, Hel och Stor.</p>

<p><strong>Halv- och helförsäkring</strong></p>

<p>Innehåller, utöver den obligatoriska trafikförsäkringen, moment som brand, glasrutor, stöld och inbrott, motor och elektronik, drulle, bärgning, rättsskydd och krishjälp.</p>

Helförsäkringen innehåller dessutom vagnskada.

<p><strong>Bilförsäkring Stor</strong></p>

<p>Bilförsäkring stor innehåller en rad fördelar för dig som är extra rädd om din bil. Den ger dig bland annat ersättning för en helt ny bil vid stöld eller totalskada (om bilen är max 2 år gammal och har körts max 4 000 mil). Dessutom ingår hyrbil, självriskreducering, låga självrisker samt snabbare vård för alla i bilen efter en trafikolycka.</p>

<p><strong>Hämtservice och hyrbil</strong></p>

<p>Trygg-Hansa har tagit fram en app där du kan fota din skadade bil och därefter skicka bilderna till en verkstad för bedömning. Med tillägget Hämtservice så hämtas din bil och körs till verkstaden. Tillägget ingår i hel- och halvförsäkringen. Du kan dessutom, om du har hyrbilstillägg, få en hyrbil hemkörd i samband med att din skadade bil hämtas upp.</p>

<p><strong>Märkesförsäkringar</strong></p>

<p>Trygg-Hansa erbjuder bilförsäkringar speciellt framtagna för vissa märken. Det finns till exempel märkesförsäkring för Peugeot, Fiat, Alfa Romeo, Jeep, Lancia, Chrysler och Citroën. Med den här försäkringen så kan du vara säker på att din bil repareras med originaldelar. Du får dessutom ett personligt telefonnummer till kundservice och skadeservice.</p>

<h2>Ersättningsnivåer</h2>

<ul>
<li>Personlig egendom: upp till 50 000 kr</li>
<li>Djurkollision: upp till 3 000 kr</li>
<li>Bärgning: 1 500 kr</li>
<li>Rehabiliteringshjälp: 150 000 kr per person</li>
<li>Medicinsk invaliditet: 600 000 kr per person</li>
</ul>

<h2>Tilläggsförsäkring</h2>

<p>Om du vill komplettera ditt försäkringsskydd kan du välja att teckna till någon av nedanstående tilläggsförsäkringar:</p>

<p><strong>Hyrbil</h2></strong></p>

<p>Om din bil blir stulen eller skadas får du 75% av hyrbilskostnaden ersatt i upp till 65 dagar. Du kan, om du inte vill ha hyrbil, få kontantersättning istället.</p>

<p><strong>Självriskrabatt</strong></p>

<p>Sänkt självrisk för vagnskada eller vagnskadegaranti med 3 000 kr. Gäller till exempel om du råkar ut för skadegörelse eller en viltolycka. Dessutom betalar du noll kronor i självrisk om din bil behöver bärgas.</p>

<p><strong>Utökad Personskada</strong></p>

<p>Ger dig och dina medpassagerare tillgång till vård och rehabilitering efter en bilolycka. Kostnaderna för vården betalas av Trygg-Hansa och de ser dessutom till att du får en egen rehabcoach som hjälper dig tillbaka efter olyckan.</p>

<p>Vid bestående skada kan en ersättning på upp till 600 000 kr per person betalas ut.</p>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller i de länder som är ansluta till Gröna Kort-samarbetet.</p>

<h2>Pris</h2>

<p>Du kan räkna ut vad det skulle kosta att försäkra din bil på Trygg-Hansas hemsida genom att fylla i personnummer och registreringsnummer.</p>
