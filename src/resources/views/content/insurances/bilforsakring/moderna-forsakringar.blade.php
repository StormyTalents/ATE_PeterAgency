<p>Moderna erbjuder en bred och prisvärd bilförsäkring. Deras Bilförsäkring Large har blivit topprankad av Konsumenternas Försäkringsbyrå. Med Large betalar du ingen självrisk vid stenskott eller vid krock med vilt.</p>

<p>Om du samlar dina försäkringar hos Moderna kan du dessutom få upp till 20% rabatt på din premie.</p>

<h2>Varför välja Modernas bilförsäkring?</h2>

<ul>
<li>Upp till 20% rabatt på premie om du samlar dina försäkringar</li>
<li>Du kan själv välja verkstad vid bilreparation</li>
</ul>

<h2>Innehåll</h2>

<p>Du väljer om du vill teckna trafik-, halv- eller helförsäkring. Om du kompletterar din bilförsäkring med de 3 tilläggsförsäkringarna Kombi, Allriskförsäkring bil och Trafikolycksfall så får du en Bilförsäkring Large.</p>

<p>Några guldkorn i Bilförsäkring Large:</p>

<ul>
<li>0 kronor i självrisk vid stenskott</li>
<li>Ingen självrisk vid krock med vilt</li>
<li>Du väljer själv verkstad för reparation av bil vid skada</li>
</ul>

<h2>Ersättningsnivåer</h2>

<ul>
<li>Medicinsk invaliditet: 600 000 kr</li>
<li>Vagnskada: upp till 3 000 kr</li>
<li>Allrisk: upp till 60 000 kr</li>
<li>Stöld: upp till 30 000 kr</li>
<li>Assistans: hyrbil i max 3 dagar vid driftstopp och stöld</li>
</ul>

<h2>Tilläggsförsäkring</h2>

<p>Om din bil är halv- eller helförsäkrad kan du teckna till en eller flera tilläggsförsäkringar.</p>

<p><strong>Hyrbil</strong></p>

<p>Om din bil behöver repareras eller av någon anledning ej kan användas så ersätts kostnad för hyrbil med 75%</p>

<p><strong>Trafikolycksfall och krishjälp</strong></p>

<p>Om någon som färdas i bilen råkar ut för personskada vid trafikolycka ersätts kostnader för behandling och sjukhusvistelse</p>

<p><strong>Assistans</strong></p>

<p>Ersättning av kostnader för till exempel hjulbyte vid punktering</p>

<p><strong>Allrisk bil</strong></p>

<p>Ersättning vid till exempel feltankning och borttappade bilnycklar</p>

<p><strong>Kombi</strong></p>

<p>Med kombi får du 50% i självriskreduktion om tredje man uppsåtligen har orsakat skada. Dessutom omfattas hyrbils- och assistansförsäkring.</p>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller i de länder som är ansluta till Gröna Kort-samarbetet. Du kan beställa ett Grönt Kort av Modernas kundservice.</p>

<h2>Pris</h2>

<p>Du kan räkna ut vad det kostar att försäkra din bil på Modernas hemsida genom att fylla i ditt personnummer och registreringsnummer. Priset baseras bland annat på val av självrisk, var du bor samt hur långt du kör.</p>
