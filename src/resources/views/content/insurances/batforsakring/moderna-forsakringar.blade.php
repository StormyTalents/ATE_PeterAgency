<p>Moderna förmedlar sina båtförsäkringar genom det egna varumärket Atlantica. Företaget har erbjudit båtförsäkringar sedan 1916. Med Atlantica Assistans kan du få snabb hjälp om det krisar och du till exempel behöver en reservdel eller en dunk bensin.</p>

<p>Om du är säkerhetsmedveten kan du få bra rabatter på din premie.</p>

<h2>Varför välja Moderna båtförsäkring?</h2>

<ul>
<li>Assistans dygnet runt</li>
<li>Välj självrisk och påverka din premie</li>
<li>Över 100 års erfarenhet</li>
</ul>

<h2>Innehåll</h2>

<p>I båtförsäkringen hos Moderna ingår en rad olika moment:</p>

<p><strong>Ansvarsförsäkring</strong></p>
<p>Ekonomiskt skydd mot skadeståndskrav</p>

<p><strong>Brand</strong></p>
<p>Ersättning vid skador som uppstått av brand, explosion eller blixtnedslag</p>

<p><strong>Stöld och skadegörelse</strong></p>
<p>Ersättning om du utsätts för stöld och eventuella skador som uppstått vid stöld och skadegörelse</p>

<p><strong>Glasskada</strong></p>
<p>Ersättning för skador som uppstått på glas, till exempel vindruta, glasdörr och jagarskärm</p>

<p><strong>Sjöskada</strong></p>
<p>Ersättning för skador som uppstått vid till exempelvis grundstötning</p>

<p><strong>Atlantica Assistans</strong></p>
<p>En larmcentral som är bemannad dygnet runt som kan hjälpa till vid bogsering, hemtransport eller leverans av reservdelar/bränsle</p>

<p><strong>Rättsskydd</strong></p>
<p>Ersättning för juridisk hjälp vid tvister</p>

<p><strong>Uppläggnings-, sjösättnings- och transportskada</strong></p>
<p>Ersättning för skador vid uppläggning, sjösättning och transport av båt</p>

<p><strong>Lösöre</strong></p>
<p>Ersättning på upp till 25 000 kronor för lösöre (personlig egendom)</p>

<p><strong>Jolle</strong></p>
<p>Jolle med en motor upp till 20 hk och med ett värde på max 20 000 kronor ingår i försäkringen</p>

<h2>Tilläggsförsäkring</h2>

<p>Moderna erbjuder även en rad olika tilläggsförsäkringar som du kan teckna för att komplettera ursprungsförsäkringen. Du kan bland annat teckna tillägg för maskinskada, allrisk, olycksfall, trailer, uthyrning och utökat geografiskt område.</p>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller i Sverige, Danmark, Finland, Norges fastland samt respektive lands territorialvatten.</p>

<h2>Pris</h2>

<p>På Atlanticas hemsida kan du räkna ut vad det skulle kosta att försäkra din båt genom att fylla i båtmodell, märke på motor, värde på båt + motor samt ditt personnummer/födelseår.</p>

<p>Du kan få rabatt på din försäkringspremie om du är säkerhetsmedveten.</p>

<p>Saker som kan ge dig rabatt:</p>

<ul>
<li>10% rabatt på premie om du använder kölskydd från Keel-pro</li>
<li>10% rabatt på premie om du installerar en startspärr</li>
<li>5% rabatt på premie om motor är stöldskyddsmärkt med Securmark. Om även båten stöldmärks blir den totala rabatten 10%</li>
<li>10% rabatt på premie om din båt har söksystem</li>
<li>10% rabatt på premie om båten vinterförvaras</li>
</ul>