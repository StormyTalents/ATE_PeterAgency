<p>Hos IF kan du välja mellan tre olika paket när du köper båtförsäkring. Du kan få rabatt på din försäkring baserat på hur säkerhetsmedveten du är. Till exempel kan du få 5% rabatt och 5 000 kr lägre självrisk om din båt har spårsändare installerad.</p>

<p>IFs båtförsäkring erbjuder ett brett skydd för din båt, men vi kan inte hitta någonting som sticker ut nämnvärt jämfört med andra båtförsäkringar på marknaden.</p>

<h2>Varför välja IF båtförsäkring?</h2>

<ul>
<li>Rabatt om din båt har installerad spårsändare</li>
<li>Försäkringen gäller även båt som ligger kvar i vattnet under vintern</li>
</ul>

<h2>Innehåll</h2>

<p>IFs båtförsäkring går att teckna i tre olika paket:</p>

<p><strong>Småbåt Kasko</strong></p>

<p>Anpassad för lite mindre båtar med ett försäkringsbelopp på högst 25 000 kronor och en motoreffekt på max 25 hk. Följande moment omfattas:</p>

<ul>
<li>Ansvar</li>
<li>Brand, stöld och skadegörelse</li>
<li>Sjöskador</li>
<li>Uppläggning och transport</li>
<li>Rutskada</li>
<li>Rättsskydd</li>
</ul>

<p><strong>Båtförsäkring</strong></p>

<p>Anpassad för dig som har en vanlig fritidsbåt för privatbruk. Innehåller samma moment som Småbåt Kasko, men erbjuder dessutom IF Båtassistans. En assistans som innebär att du får tillgång till en service som finns tillgänglig dygnet runt. Du kan få hjälp med bogsering, hemtransport eller leverans av bränsle/reservdelar.</p>

<p><strong>Stor Båtförsäkring</strong></p>

<p>Ett omfattande försäkringspaket för din båt som skyddar såväl båten som personerna ombord. Innehåller allt som ingår i Småbåt Kasko och Båtförsäkring. Dessutom ingår en olycksfallsförsäkring, maskinskydd och något som kallas BåtXtra.</p>

<p>I BåtXtra ingår:</p>

<ul>
<li>Båtotur – Gäller vid skada på eller förlust av navigationsutrustning.</li>
<li>Avbrottsersättning i samband med stöld – Gäller vid stöld av hela båten eller av huvudmotor</li>
</ul>

<h2>Tilläggsförsäkring</h2>

<p>BåtXtra och Olycksfall var tidigare tilläggsförsäkringar, men ingår nu i Stor Båtförsäkring.</p>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller i Sverige, Danmark, Finland och Norge och respektive lands territorialvatten. Den gäller även i Östersjön och dess angränsande vikar och hav, Kattegatt och Skagerack samt i inre tyska farvatten.

<p>Om du har en segelbåt som är minst 9 meter lång och högst 30 år gammal gäller försäkringen även i Nordsjön (mellan maj – augusti). Under förutsättning att den inte används för att delta i kappsegling.</p>

<p>För övriga båttyper kan du inte utöka det geografiska skyddet vilket är något som går att lägga till för många andra båtförsäkringar på marknaden.</p>

<h2>Pris</h2>

<p>På IFs hemsida kan du kostnadsfritt räkna ut vad det skulle kosta att försäkra din båt. Du fyller i båtens fabrikat, antal huvudmotorer samt ditt personnummer. IF ger dig 10% rabatt på din försäkring första året.</p>

<p>Du kan dessutom få rabatt om du är säkerhetsmedveten. Du kan få 5% rabatt och 5 000 kr lägre i självrisk om du använder dig av en spårsändare.</p>