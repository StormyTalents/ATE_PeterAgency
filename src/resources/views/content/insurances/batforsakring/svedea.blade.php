<p>Svedea är specialiserade på sakförsäkringar och har tagit fram konkurrenskraftiga försäkringar inom flera olika nischer. Deras skadeservice utsågs 2017 till den snabbaste och mest kompetenta skaderegleringen av Svenskt Kvalitetsindex.</p>

<p>När det gäller deras båtförsäkring har de tänkt på flera saker som gör att de sticker ut gentemot många av sina konkurrenter. Om du är säkerhetsmedveten kan du få bra rabatter på din försäkringspremie. De har även tagit fram något som de kallar Starthjälpen som är en hjälpande hand om din båtmotor inte startar.</p>

<h2>Varför välja Svedea båtförsäkring?</h2>

<ul>
<li>Rabatt om din båt är skyddad med startspärr eller har stöldskyddsmärkning</li>
<li>Svedea håller koll på marknadsvärdet på din båt</li>
<li>Starthjälp om båtmotorn vägrar starta</li>
<li>Märkesförsäkringar särskilt framtagna för flera olika båtmärken</li>
<li>Prisbelönt skadeservice</li>
</ul>

<h2>Innehåll</h2>

<p>Svedea erbjuder en bred båtförsäkring där du kan välja mellan 3 olika paket: L, XL och XXL.</p>

<p>Moment som ingår i paket L:</p>

<p><strong>Sjöskada</strong></p>
<p>Ersättning om din båt utsätts för en plötslig och utifrån kommande händelse. Det innefattar bland annat kollision, strandning och kantring.</p>

<p><strong>Uppläggnings- och transportskada</strong></p>
<p>Ersättning om din båt skadas i samband med lyft, transport eller uppställning. Såvida skadan orsakas av en plötslig och utifrån kommande händelse.</p>

<p><strong>Stöld och skadegörelse</strong></p>
<p>Om din båt, motor eller tillbehör stjäls eller om de drabbas av skadegörelse kan du få ersättning.</p>

<p><strong>Brand</strong></p>
<p>Ersättning om din båt tar skada på grund av brand, explosion eller blixtnedslag.</p>

<p><strong>Ansvar</strong></p>
<p>Ersättning om du blir skadeståndsskyldig gentemot annan person eller egendom.</p>

<p><strong>Rättsskydd</strong></p>
<p>Ersättning för advokat- och rättegångskostnader om det uppstår tvister kring till exempel köp eller försäljning av båt.</p>

<p>Om du väljer XL eller XXL får du ett mer omfattande skydd som kan ge ersättning vid olycksfall, assistans och om båten är obrukbar i mer än 20 dagar i samband med att den lämnas in för reparation. Dessutom får du dubbelt försäkringsvärde på personlig lösegendom samt en allriskförsäkring som ger ersättning för skador på båten.</p>

<p>Med XXL får du dessutom ersättning vid skada på all maskinell utrustning.</p>

<p><strong>Svedea håller koll på din båts marknadsvärde</strong></p>
<p>Du behöver inte rapportera in din båts värde till Svedea varje år. De håller själva koll på utvecklingen av marknadsvärdet.</p>

<p><strong>Märkesförsäkringar</strong></p>

<p>Svedea har tagit fram flera märkesförsäkringar i samarbete med följande märken: Anytec, Arronet, BRIG, Micore, Ryds och Sea Ray. Gäller även om du köper en båt med Suzuki-motor eller Yamaha-motor. Märkesförsäkringen gäller om båten köps hos en auktoriserad återförsäljare.</p>

<p>Några av fördelarna med märkesförsäkringarna är att du får ett bättre pris samt att du kan få snabbare ersättning vid stöld.</p>

<h2>Tilläggsförsäkring</h2>

<p>Det finns möjlighet att utöka din försäkring om du till exempel vill hyra ut din båt eller om du planerar att köra båten i Nordsjön eller Medelhavet. Du kan också köpa en tilläggsförsäkring som omfattar skador på trailer.</p>

<h2>Var gäller försäkringen?</h2>

<p>Svedeas båtförsäkring gäller i Sverige, Danmark och Finland samt i respektive lands territorialvatten. Dessutom så gäller försäkringen inom Norges fastland och territorialvatten.</p>

<h2>Pris</h2>

<p>På Svedeas hemsida kan du räkna ut vad det kostar att försäkra din båt genom att fylla i båtmodell och personnummer.</p>

<p>Har du stöldskydd på båten kan du få upp till 30% rabatt på din försäkring. Du kan även få pengar avsatta till ett självriskkonto efter varje skadefritt år du har. Det som avsätts är 10% av din aktuella självrisk för sjöskador.</p>