<p>
    Unionen Egenföretagare är ett medlemskap hos fackförbundet Unionen som är helt anpassat till dig som är egenföretagare. Fördelarna med medlemskapet är: Trygghet – Affärsverktyg – Kompetensutveckling
</p>        

<h2>Vad kostar det?</h2>
<p>
    Avgiften är uppdelad i en förbundsavgift på 125 kr/månaden samt Serviceavgift på 250 kr/månad. Av dessa avgifter är Serviceavgiften möjlig att lägga på företaget.
</p>

<h2>Vilka företag passar för Unionen Egenföretagare?</h2>
<p>
    Om du idag har ett företag utan några anställda passar Unionen Egenföretagare perfekt. Ditt företag ska hittas inom det privata näringslivet och vara verksam inom ett av de områden som Unionen arbetar inom. Då de är verksamma inom ett stort antal branscher kan de flesta egenföretagare söka medlemskap.  
</p>
<p>
    Exempelvis är de verksamma mot industri, handel, IT, media, bygg, transport, service och konsultverksamhet.
</p>

<h2>Varför bli medlem? (fördelar)</h2>
<ul>
<li>
<h3> Inkomstförsäkring</h3>
<p>Alla medlemmar får en inkomstförsäkring. Därmed kompletterar denna försäkring din ersättning från A-kassan så du får 80 % av din lön. Detta upp till 60 000 kr i månaden i ca 150 dagar. Det går även att teckna tillägg som förbättrar inkomstförsäkringen ytterligare. (Kräver medlemskap i A-kassa)</p>
</li>
<li>
<h3>Billigare försäkringar</h3>
<p>Få rabatterade priser på personförsäkringar som kan tecknas till dig och din familj. Tack vare att Unionen kan förhandla fram stora avtal kan de även erbjuda bättre priser än dom du gått direkt till försäkringsbolaget. </p>
<p>Är du medlem kan du även teckna sjukvårdsförsäkring via Bliwa till rabatterat pris. Det ger dig snabbare vård vilket betyder att du kan återgå till arbetet snabbare efter sjukskrivning. Att vara borta för länge från arbetet som egenföretagare kan annars innebära att kunder söker sig till andra leverantörer. </p>
<p>På småföretagarförsäkring ges 20% om den tecknas hos Folksam. </p>
<p>Tre månader gratis försäkringspaket &#8211; Alla medlemmar får tre månaders gratis försäkringsskydd vilket består av olycksfalls-, liv-, sjuk- och diagnosförsäkring. Utöver det även sjukkapital. </p>
</li>
<li>
<h3>Skräddarsydda avtal</h3>
<p>Få tillgång till alla skräddarsydda avtal som kan laddas ner via avtal24.se</p>
</li>
<li>
<h3>Gratis juridisk rådgivning</h3>
<p>Varje kalenderår kan 10 timmars gratis rådgivning användas. Detta via advokatbyrån Fylgia. De kan exempelvis hjälpa till med avtal, hur du kan agera när kunder inte betalar, rättighetsfrågor m.m. Dessutom ges 20% hos Fylgia i det fall man vill anlita denna advokatbyrå. </p>
</li>
<li>
<h3>Affärscoaching av proffs</h3>
<p>Få råd och hjälp inom områden som marknadsföring, affärsutveckling och försäljning via de certifierade coacher som medlemmarna kan rådgöra med kostnadsfritt. </p>
</li>
<li>
<h3>Förbättra din LinkedIn-profil</h3>
<p>Få hjälp via företaget Go Monday med att se över din LinkedIn-profil. En väg att hitta nya kunder och skapa det livsnödvändiga nätverk som företagare är behov av. </p>
</li>
<li>
<h3>Företagsekonomisk rådgivning</h3>
<p>I medlemskapet ingår en gratis rådgivningstimme per år hos Wrebit. Här kan man diskutera frågor kring företagsekonomisk karaktär. </p>
</li>
<li>
<h3>Möte med mentor</h3>
<p>Träffa en mentor från Mentorerna.se som kan hjälpa dig att utveckla företaget och ta nya steg. Första träffen är gratis. Därefter ges rabatt på kommande tillfällen. </p>
</li>
<li>
<h3> Kompetensbidrag</h3>
<p>Vill du gå en kurs eller höja din kompetens på annat sätt? Varje år kan man totalt ansöka om 13 300 kr för kompetenshöjande åtgärder. </p>
</li>
<li>
<h3>Tidningen Driva Eget</h3>
<p>Åtta gånger per år kommer företagartidningen Driva Eget med olika teman. Med intervjuer, fakta och artiklar skapas en mix av information som många småföretagare uppskattar. </p>
<p>Samt en hel del andra rabatter och förmåner…</p>
</li>
</ul>
<p><strong>Kortfattat ger Unionen Egenföretagare stöd och hjälp till de företagare som är medlemmar. Detta både via försäkringar, rådgivning och kompetenshöjande åtgärder. </strong></p>
