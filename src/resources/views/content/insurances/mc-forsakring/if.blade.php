<p>Hos IF kan du teckna en MC-försäkring som täcker det mesta du kan tänkas behöva. IF har inget krav på att motorcykeln måste förvaras i ett garage vilket är ett krav i många andra bolags MC-försäkringar. Dessutom så erbjuder de en märkesförsäkring specifikt framtagen för dig som kör en BMW motorcykel.</p>

<h2>Varför välja IFs MC-försäkring?</h2>
<ul>
<li>Inget krav på garage</li>
<li>En märkesförsäkring skräddarsydd för BMW motorcyklar</li>
<li>Släpvagn är medförsäkrad</li>
</ul>

<h2>Innehåll</h2>

<p>IFs MC-försäkring är bred och innehåller ett par unika aspekter som gör den att den sticker ut från andra MC-försäkringar på marknaden.</p>

<p>Som nämnt ovan så har IF inget krav på att motorcykeln står inlåst i ett garage vilket underlättar för dig som inte har tillgång till ett garage. Många andra bolag kräver att MC:n förvaras inlåst i ett garage över natten.</p>

<p>IF har i samarbete med BMW tagit fram en MC-försäkring som är speciellt framtagen för dig som kör en BMW motorcykel. Några guldkorn som ingår i den försäkringen:</p>

<ul>
<li>Bärgning och räddning helt utan självrisk i alla länder som ingår i Gröna Kort-samarbetet</li>
<li>Maskinskadeförsäkring som gäller i upp till 8 år och upp till 10 000 mil</li>
<li>Inget krav på att MC:n förvaras i ett garage</li>
<li>Sanering vid feltankning på MC som är mindre än 15 år gammal</li>
</ul>

<p>En annan sak som ingår i IFs MC-försäkring är möjligheten att medförsäkra en oregistrerad släpvagn som är avsedd att dras av motorcykeln. Det högsta ersättningsbeloppet uppgår till 20 000 kronor.</p>

<h2>Ersättningsnivåer</h2>

<ul>
<li>Medicinsk invaliditet: Upp till 600 000 kr</li>
<li>Ersättning vid dödsfall: 50 000 kr</li>
<li>Motorcykel äldre än 30 år: Upp till 60 000 kr</li>
<li>Feltankning: Upp till 50 000 kr</li>
</ul>

<h2>Tilläggsförsäkring</h2>

<p>Om du har MC-försäkring hos IF kan du välja att köpa till en olycksfallsförsäkring. Försäkringen ger en extra ersättning på upp till 600 000 kronor, utöver det som ingår i trafikförsäkringen. Ersättningen utgår vid personskada som leder till medicinsk invaliditet eller dödsfall. Dessutom ersätts kostnad för kristerapi av psykolog i Sverige. Upp till 10 behandlingstillfällen per försäkrad.</p>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller i de länder som ingår i Gröna Kort-samarbetet. Dock så gäller den inte i Iran, Marocko och Tunisien.</p>

<h2>Pris</h2>

<p>På IFs hemsida kan du räkna ut vad det skulle kosta att försäkra just din MC genom att fylla i ditt personnummer och registreringsnummer. Priset är baserat på motorcykelns tjänstevikt, effekt och ålder. Det baseras också på din ålder, var du bor, hur länge du har haft körkort och om det finns skadehistorik.</p>