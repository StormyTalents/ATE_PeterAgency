<p>Försäkringsbolaget Gjensidige har funnits i över 200 år och är ett av de ledande sakförsäkringsbolagen i Norden. Deras MC-försäkring innehåller ett brett skydd med låga självrisker. I övrigt är det inte mycket som gör Gjensidiges MC-försäkring unik jämfört med andra MC-försäkringar på marknaden.</p>

<h2>Varför välja Gjensidige MC-försäkring?</h2>

<ul>
<li>Låga självrisker vid skador</li>
</ul>

<h2>Innehåll</h2>

<p>Trafikförsäkring är obligatorisk och är inkluderad i alla Gjensidiges MC-försäkringar. Utöver det kan du välja till hel- eller halvförsäkring. Trafikförsäkringen täcker personskador på föraren samt passagerare och medtrafikanter. Dessutom ersätts skador på annans egendom i samband med olycka.</p>

<h2>Tilläggsförsäkring</h2>

<p>Ingen tilläggsförsäkring finns tillgänglig.</p>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller i de länder som är med i Gröna Kort-samarbetet.</p>

<h2>Pris</h2>

<p>Priset på din MC-försäkring baseras på typ av motorcykel, motorcykelns vikt och effekt samt din ålder.</p>

<p>För att få prisuppgift och kunna teckna försäkringen måste du kontakta Gjensidige via deras hemsida.</p>
