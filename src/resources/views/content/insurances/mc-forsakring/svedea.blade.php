<p>Svedea är specialiserade på sakförsäkringar och kan erbjuda lite mer konkurrenskraftiga försäkringar inom vissa nischer. Med deras prisvärda MC-försäkring ges du ett brett skydd med låga självrisker. Svedeas skadeservice blev under 2017 utsedd till den snabbaste och mest kompetenta skaderegleringen av Svenskt Kvalitetsindex.</p>

<h2>Varför välja Svedeas MC-försäkring?</h2>

<ul>
<li>Prisvärd MC-försäkring med ett brett skydd</li>
<li>Prisbelönt skadeservice</li>
<li>Erbjuder stilleståndsersättning</li>
<li>Möjlighet att själv påverka priset</li>
</ul>

<h2>Innehåll</h2>

<p>Svedea erbjuder en bred MC-försäkring med låga självrisker. Utöver det ursprungliga skyddet kan du välja att teckna en tilläggsförsäkring. Läs mer om vad den innefattar längre ner.</p>

<p>Ett urval över vad som ingår:</p>

<p><strong>Maskinskadeförsäkring</strong></p>

<p>Gäller i upp till 6 år (eller max 6000 mil) och gäller vid plötslig och oförutsedd skada som drabbar motorcykelns funktion.</p>

<p><strong>Stöldförsäkring</strong></p>

<p>Ersättning om din mc blir stulen eller om den blir skadad under ett stöldförsök. Dessutom omfattas utrustning och tillbehör. Om mc:n inte hittas får du ersättning 30 dagar efter anmälan till polisen och Svedea.</p>

<p><strong>Allrisk</strong></p>

<p>Ersättning utgår vid:</p>

<ul>
<li>Förlust eller skada på personlig egendom tillhörande förare/passagerare</li>
<li>Feltankning</li>
<li>Borttappad nyckel.</li>
</ul>

<h2>Ersättningsnivåer</h2>

<ul>
<li>Medicinsk invaliditet: 1 000 000 kr</li>
<li>Amatör/ombyggd motorcykel: 150 000 kr</li>
<li>MC Retro: 150 000 kr</li>
<li>Tillbehör och utrustning: 20 000 kr (vid XL: 80 000 kr)</li>
<li>Allriskskada: 15 000 kr</li>
<li>Dödsfall: 100 000 kr</li>
</ul>

<h2>Tilläggsförsäkring</h2>

<p>Om du har hel- eller halvförsäkring kan du välja att köpa tillägget XL. Då ingår följande:</p>

<ul>
<li>Räddnings- och bärgningsassistans dygnet runt – helt utan självrisk</li>
<li>Kris- och olycksfallsförsäkring</li>
<li>Maxersättning för utrustning och tillbehör höjs till 80 000 kronor</li>
<li>Hyrbilsersättning förlängs med 14 dagar (till totalt 21 dagar)</li>
<li>Ingen självrisk på vagnskada vid djurkollision vid innehav av helförsäkring</li>
</ul>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller inom EES-området (EU, Island, Liechtenstein och Norge), Schweiz och i ett antal medelhavsländer under maximalt 90 dagar per försäkringsår. Vid längre vistelse behöver du kontakta Svedea.</p>

<h2>Pris</h2>

<p>Priset baseras på en rad olika faktorer. Bland annat åldern på motorcykeln, modell, effekt och vikt samt hur gammal du är och var du är bosatt. Priset kan i vissa fall även baseras på hur länge du har haft MC-kort och hur skadehistoriken ser ut.</p>

<p>Du kan själv påverka priset på din MC-försäkring genom val av förare, hur motorcykeln parkeras och förvaras över natten, personlig skyddsutrustning samt val av självrisknivå för vagnskada.</p>

<p>Priset är säsongsbaserat vilket innebär att du betalar mer på sommaren än på vintern.</p>

<p>Om du är medlem i SMC får du 10% rabatt på din försäkring.</p>

<p>Se ditt pris på hemsidan genom att fylla i ditt registreringsnummer och personnummer.</p>