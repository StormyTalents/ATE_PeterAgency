<p>Dina försäkringar har flera gånger belönats med olika priser och utmärkelser. De har även bra lokal närvaro med cirka 50 kontor utspridda i landet. Deras MC-försäkring ger dig rabatt om du är säkerhetsmedveten och väljer en motorcykel som har ABS-bromsar och antispinnfunktion.</p>

<h2>Varför välja Dina Försäkringar MC-försäkring?</h2>

<ul>
<li>Rabatt om du har ABS-bromsar och/eller antispinnfunktion</li>
<li>Ingen självrisk vid stöld om din MC har en monterad spårsändare</li>
</ul>

<h2>Innehåll</h2>

<p>Ett urval av saker som ingår i försäkringen:</p>

<p><strong>Maskin- och elektronikskada</strong></p>

<p>Gäller motorcykel som körts högst 6000 mil eller till det år motorcykeln är 5 år gammal. Ersättning ges vid skada eller fel som påverkar bland annat motor, kraftöverföring och bromsar.</p>

<p><strong>Vagnskadeförsäkring</strong></p>

<p>Gäller för skada vid trafikolycka eller annan yttre olyckshändelse.</p>

<p><strong>Stöld</strong></p>

<p>MC-försäkringen ger ersättning vid stöld. Ersättning betalas ut om fordonet inte går att återfinna inom en månad. Om MC:n har en spårsändare monterad behöver du som försäkringstagare inte betala någon självrisk.</p>

<h2>Tilläggsförsäkring</h2>

<p>Om du vill komplettera skyddet i ursprungsförsäkringen kan du välja att teckna dessa tilläggsförsäkringar:</p>

<ul>
<li>Djurkollisionsförsäkring – Ersättning på upp till 6 000 kronor vid plötslig och oförutsedd kollision med djur</li>
<li>Assistansförsäkring – Ersättning för självriskkostnad när du utnyttjar räddningsförsäkring</li>
</ul>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller i de länder som ingår i Gröna Kort-samarbetet.</p>

<h2>Pris</h2>

<p>Om du har ABS-bromsar får du 25% rabatt på försäkringen. Har du dessutom antispinnfunktion får du ytterligare 20% rabatt.</p>

<p>Priset på din försäkring baseras på typ av motorcykel, motorcykelns ålder, din ålder och var du är bosatt.</p>

<p>Du kan räkna ut vad det skulle kosta att försäkra din motorcykel på deras hemsida.</p>
