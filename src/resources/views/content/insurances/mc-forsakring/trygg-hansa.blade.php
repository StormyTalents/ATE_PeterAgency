<h2>Innehåll</h2>

<p>Försäkringen gäller både för lätt och tung motorcykel. Om du krockar eller om motorcykeln på något sätt skadas ingår det bärgning till närmaste verkstad. Dessutom kan du få krishjälp om du mår psykiskt dåligt efter en olycka.</p>

<h2>Tilläggsförsäkring</h2>

<p>Om du väljer att köpa tilläggsförsäkring får både du och din passagerare kortare väntetider efter en olycka. Du får även vårdkostnaderna betalda samt en egen rehabcoach med personlig hjälp för att komma tillbaka efter olyckan. Vid bestående skador kan ni även få ersättning på upp till 600 000 kr, beroende på hur pass allvarlig skadan är.</p>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller i alla länder som är anslutna till det Gröna Kort-samarbetet.</p>

