<p>Hos ICA kan du försäkra din motorcykel till ett bra pris och du har även möjlighet att själv välja innehållet i försäkringen. Har du dessutom ett ICA-kort kan du få bonus på försäkringskostnaden och du får även dubbel bonus när du använder ditt ICA-kort för att handla i ICA-butikerna.</p>

<h2>Varför välja ICA:s MC-försäkring?</h2>

<ul>
<li>Bonus på försäkringskostnaden om du har ICA-kort</li>
<li>Dubbel bonus när du handlar med ditt ICA-kort på en ICA-butik</li>
<li>Du kan själv välja önskad självrisk för plåt och lackskada</li>
<li>Ingen självrisk vid stöld om MC har godkänd spårsändare</li>
<li>Ingen självrisk vid krock med vilt</li>
</ul>

<h2>Innehåll</h2>

<p>ICA erbjuder en bred MC-försäkring där en av fördelarna är att du slipper självrisk vid stöld om din MC har en godkänd spårsändare monterad. Du har även möjlighet att själv påverka priset på din försäkring genom val av självrisk.</p>

<h2>Ersättningsnivåer</h2>

<ul>
<li>Medicinsk invaliditet: 600 000 kr</li>
<li>Amatör/ombyggd motorcykel: 60 000 kr</li>
<li>Dödsfall vid trafikolycka: 20 000 kr</li>
<li>Ersättning vid sjukhusvistelse: 3600 kr vid 72 timmar och 2000 vid sjukskrivning i minst 7 dagar</li>
</ul>

<h2>Tilläggsförsäkring</h2>

<p>MC-försäkringen har ingen tilläggsförsäkring.</p>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller i de länder som ingår i Gröna Kort-samarbetet.</p>

<h2>Pris</h2>

<p>På ICA:s hemsida kan du räkna ut vad det kostar att försäkra din MC genom att fylla i ditt personnummer och registreringsnummer. Priset baseras på din ålder, typ av motorcykel, folkbokföringsadress samt vilken självrisk du väljer.</p>

<p>Priset är säsongsbaserat vilket innebär att priset är lägre under vintermånaderna då MC:n inte används lika mycket under den perioden.</p>