<p>Hos Länsförsäkringar kan du välja mellan tre olika MC-försäkringar och därmed få en omfattning som uppfyller just de behov du har. Om du är mer säkerhetsmedveten kan du till exempel få en billigare försäkring. Med Länsförsäkringars MC-försäkring kan du få assistans dygnet runt om du drabbas av ett driftstopp.</p>

<h2>Varför välja Länsförsäkringars MC-försäkring?</h2>

<ul>
<li>Välj en försäkringsomfattning som passar dig</li>
<li>Assistans dygnet runt vid driftstopp</li>
</ul>

<h2>Innehåll</h2>

<p>Du väljer själv en av följande försäkringar:</p>

<p><strong>MC Smart:</strong></p>
<p>En årsförsäkring där priset är säsongsanpassat. Försäkringen är anpassad efter dig som är extra mån om säkerheten. Förutsättningen är att du använder dig av skyddsutrustning, förvarar motorcykeln inlåst under natten och bara lånar ut motorcykeln till personer inom din familj. Priset på försäkringen blir därav billigare.</p>
<p>MC Smart finns som hel- eller halvförsäkring.</p>

<p><strong>Årsförsäkring:</strong></p>
<p>Anpassad efter dig som vill köra din motorcykel året runt. Årsförsäkringen kostar mindre än Basförsäkringen.</p>
<p>Årsförsäkringen finns som trafik, hel- eller halvförsäkring</p>

<p><strong>Basförsäkring:</strong></p>
<p>Passar dig som ställer på och av din motorcykel inför vår och höst.</p>
<p>Basförsäkringen finns som trafik, hel- eller halvförsäkring.</p>

<h2>Ersättningsnivåer</h2>

<ul>
<li>Motorcykel äldre än 1980: Upp till 75 000 kronor</li>
<li>Sanering av tank: Upp till 6 000 kronor</li>
<li>Krishantering: 10 betalda behandlingstillfällen</li>
</ul>

<h2>Tilläggsförsäkring</h2>

<p>Länsförsäkringar erbjuder ingen tilläggsförsäkring för MC.</p>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller i de länder som är ansluta till Gröna Kort-samarbetet.</p>

<h2>Pris</h2>

<p>Du kan räkna ut priset på Länsförsäkringars hemsida genom att fylla i ditt registreringsnummer och personnummer. Priset baseras på motorcykelns vikt, motoreffekt och årsmodell samt på din ålder, var du bor och vilken självrisk du väljer.</p>
