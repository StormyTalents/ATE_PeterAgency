<p>Moderna förmedlar sin bil- och MC-försäkring genom varumärket Bilsport & MC som är speciellt nischad mot just den branschen. MC-försäkringen är bred och erbjuder en rad olika fördelar.</p>

<h2>Varför välja Modernas MC-försäkring?</h2>

<ul>
<li>Möjlighet att själv påverka din premie</li>
<li>Du kan själv välja verkstad vid skada</li>
<li>Ingen självrisk vid viltolycka</li>
<li>Ersättning till nyvärde i upp till 24 månader</li>
<li>Ersättning redan inom 14 dagar vid stöld</li>
</ul>

<h2>Innehåll</h2>

<p>Som nämnt ovan så ger Modernas MC-försäkring ett brett skydd. Något som sticker ut jämfört med många konkurrenter är att du betalar samma pris oavsett om du lånar ut din motorcykel till en annan förare, använder skyddskläder eller om du har MC:n inlåst under nätterna i din hemort. Om du drabbas av skada så påverkas istället självrisken.</p>

<p>Ett annat guldkorn är nyvärdesersättningen du får om din gamla MC blir helt förstörd. Du ersätts då med en annan mc av samma typ och årsmodell.</p>

<p>Utöver ursprungsförsäkringen kan du välja att köpa en tilläggsförsäkring.</p>

<h2>Tilläggsförsäkring</h2>

<ul>
<li>Högre ersättning för extrautrustning och tillbehör till motorcykeln</li>
<li>En kompletterande olycksfallsförsäkring som ger upp till 800 000 kr vid medicinsk invaliditet och 9 000 kr vid sjukhusvistelse som varar i minst 3 dygn</li>
<li>Ett kompletterande efterlevandeskydd som ger upp till 100 000 kr för förare och passagerare vid dödsfall</li>
<li>Fri assistanshjälp utan självrisk och rätt till hyrbil/hyrmotorcykel i upp till 21 dygn</li>
</ul>

<h2>Var gäller försäkringen?</h2>

<p>Försäkringen gäller i länder som ingår i Gröna Kort-samarbetet.</p>

<h2>Pris</h2>

<p>Genom att fylla i ditt personnummer och registreringsnummer på Modernas hemsida kan du få förslag på hur mycket det skulle kosta att försäkra just din MC.</p>

<p><strgon>Du kan ta del av flera olika rabatter:</strong></p>

<ul>
<li>Om du försäkrar fler fordon</li>
<li>Om du har fler försäkringar tecknade hos Moderna</li>
<li>Om du är med i någon av de klubbar Moderna samarbetar med</li>
<li>Om du är med i Sveriges Motorcyklister (SMC)</li>
</ul>

<p>Du kan få upp till 20% i rabatt per tecknad försäkring. Vissa begränsningar över vilka rabatter som kan kombineras förekommer.</p>
